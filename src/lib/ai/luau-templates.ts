export interface InventoryParams {
  maxSlots: number
  categories: string[]
  stackable?: boolean
}

export function inventorySystem(params: InventoryParams): string {
  const maxSlots = Math.max(1, Math.min(100, params.maxSlots))
  const categories = params.categories.map(safe)

  return `-- Inventory System (ServerScript → ServerScriptService)
local Players = game:GetService("Players")
local DataStoreService = game:GetService("DataStoreService")

local inventoryStore = DataStoreService:GetDataStore("Inventory_v1")
local MAX_SLOTS = ${maxSlots}
local CATEGORIES = {"${categories.join('", "')}"}
local STACKABLE = ${params.stackable ? 'true' : 'false'}

export type InventoryItem = {
  id: string,
  name: string,
  category: string,
  quantity: number,
  metadata: {[string]: any}?,
}

local inventories: {[number]: {[number]: InventoryItem?}} = {}

local function loadInventory(player: Player)
  local userId = player.UserId
  local success, data = pcall(function()
    return inventoryStore:GetAsync("inv_" .. userId)
  end)
  inventories[userId] = (success and type(data) == "table" and data) or {}
end

local function saveInventory(player: Player)
  local userId = player.UserId
  if not inventories[userId] then return end
  pcall(function()
    inventoryStore:SetAsync("inv_" .. userId, inventories[userId])
  end)
end

local function getInventory(player: Player): {[number]: InventoryItem?}
  return inventories[player.UserId] or {}
end

local function findItem(player: Player, itemName: string): (number?, InventoryItem?)
  local inv = inventories[player.UserId]
  if not inv then return nil, nil end
  for slot, item in ipairs(inv) do
    if item and item.name == itemName then return slot, item end
  end
  return nil, nil
end

local function findEmptySlot(player: Player): number?
  local inv = inventories[player.UserId]
  if not inv then return nil end
  for i = 1, MAX_SLOTS do
    if not inv[i] then return i end
  end
  return nil
end

local function addItem(player: Player, item: InventoryItem): (boolean, string)
  local userId = player.UserId
  if not inventories[userId] then return false, "Player not found" end

  if STACKABLE then
    local slot, existing = findItem(player, item.name)
    if existing then
      existing.quantity = existing.quantity + item.quantity
      return true, "Stacked"
    end
  end

  local slot = findEmptySlot(player)
  if not slot then return false, "Inventory full" end

  inventories[userId][slot] = item
  return true, "Added"
end

local function removeItem(player: Player, itemName: string, quantity: number): boolean
  local slot, item = findItem(player, itemName)
  if not slot or not item then return false end
  if item.quantity < quantity then return false end
  item.quantity -= quantity
  if item.quantity <= 0 then
    inventories[player.UserId][slot] = nil
  end
  return true
end

-- RemoteFunction API for client inventory UI
local invRemote = Instance.new("RemoteFunction")
invRemote.Name = "InventoryAction"
invRemote.Parent = game:GetService("ReplicatedStorage")

invRemote.OnServerInvoke = function(player: Player, action: string, data: any): any
  if action == "get" then
    return getInventory(player)
  elseif action == "use" and type(data) == "table" then
    return removeItem(player, data.name, data.quantity or 1)
  end
  return false
end

-- Server API via BindableFunction
local invAPI = Instance.new("BindableFunction")
invAPI.Name = "InventoryAPI"
invAPI.Parent = game:GetService("ServerStorage")

invAPI.OnInvoke = function(action: string, player: Player, itemData: InventoryItem?, itemName: string?, qty: number?)
  if action == "add" and itemData then
    return addItem(player, itemData)
  elseif action == "remove" and itemName then
    return removeItem(player, itemName, qty or 1)
  elseif action == "has" and itemName then
    return findItem(player, itemName) ~= nil
  end
end

Players.PlayerAdded:Connect(function(p) loadInventory(p) end)
Players.PlayerRemoving:Connect(function(p) saveInventory(p); inventories[p.UserId] = nil end)

print("[Inventory] System initialized — " .. MAX_SLOTS .. " slots, categories: ${categories.join(', ')}")`
}

