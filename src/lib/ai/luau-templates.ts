export interface TreeParams {
  type: 'oak' | 'pine' | 'cherry' | 'palm' | 'birch'
  size: 'small' | 'medium' | 'large'
  withRoots: boolean
  withFallenLeaves: boolean
  season: 'spring' | 'summer' | 'autumn' | 'winter'
}

export function detailedTree(params: TreeParams): string {
  const ttype = params.type
  const tsize = params.size
  const season = params.season

  const scale = tsize === 'small' ? 0.65 : tsize === 'large' ? 1.55 : 1.0

  // Trunk color per tree type
  const trunkColorR =
    ttype === 'birch'  ? '220, 215, 205' :
    ttype === 'palm'   ? '150, 120, 80' :
    '100, 75, 50'

  // Leaf color per season
  const leafColorR =
    season === 'spring'  ? '120, 190, 90' :
    season === 'summer'  ? '55, 140, 55' :
    season === 'autumn'  ? '200, 90, 30' :
    '60, 60, 70'  // winter — bare/grey

  const leafColorVariantR =
    season === 'spring'  ? '140, 200, 100' :
    season === 'summer'  ? '70, 160, 60' :
    season === 'autumn'  ? '180, 60, 20' :
    '70, 70, 80'

  const hasLeaves = season !== 'winter'
  const trunkH = 6 * scale
  const trunkBaseW = 1.0 * scale
  const branchCount = ttype === 'pine' ? 0 : (ttype === 'palm' ? 0 : 4)

  return `-- Detailed Tree: ${ttype} (${season}) — ${tsize} size
local ChangeHistoryService = game:GetService("ChangeHistoryService")
ChangeHistoryService:SetWaypoint("Before_Tree")

local SCALE = ${scale.toFixed(2)}
local originX, originY, originZ = 0, 0, 0

local tree = Instance.new("Model")
tree.Name = "${ttype}_${season}"
tree.Parent = workspace

local function cyl(parent, nm, diam, h, cf, color)
  local p = Instance.new("Part")
  p.Name = nm
  p.Shape = Enum.PartType.Cylinder
  p.Size = Vector3.new(h, diam, diam)
  p.CFrame = cf * CFrame.Angles(0, 0, math.pi/2)
  p.Color = Color3.fromRGB(table.unpack(color))
  p.Material = Enum.Material.Wood
  p.Anchored = true
  p.TopSurface = Enum.SurfaceType.Smooth
  p.BottomSurface = Enum.SurfaceType.Smooth
  p.Parent = parent
  return p
end

local function sphere(parent, nm, size, pos, color, mat)
  local p = Instance.new("Part")
  p.Name = nm
  p.Shape = Enum.PartType.Ball
  p.Size = Vector3.new(size, size, size)
  p.CFrame = CFrame.new(pos)
  p.Color = Color3.fromRGB(table.unpack(color))
  p.Material = mat or Enum.Material.LeafyGrass
  p.Anchored = true
  p.CastShadow = true
  p.Parent = parent
  return p
end

-- ── Trunk (3 stacked cylinders, tapering) ───────────────────────────────────
local trunkH = ${trunkH.toFixed(2)}
local trunkBaseW = ${trunkBaseW.toFixed(2)}
local trunkSections = 3
for ti = 1, trunkSections do
  local segH = trunkH / trunkSections
  local segDiam = trunkBaseW * (1 - (ti - 1) * 0.22)
  local segY = originY + (ti - 1) * segH + segH / 2
  cyl(tree, "Trunk_"..ti, segDiam, segH,
    CFrame.new(originX, segY, originZ),
    {${trunkColorR}})
end

${params.withRoots ? `
-- ── Surface roots (radiating planks) ─────────────────────────────────────────
local rootAngles = {0, math.pi*0.4, math.pi*0.8, math.pi*1.2, math.pi*1.6}
for ri, angle in ipairs(rootAngles) do
  local rootLen = SCALE * (1.8 + math.random() * 0.8)
  local rootX = originX + math.cos(angle) * rootLen * 0.5
  local rootZ = originZ + math.sin(angle) * rootLen * 0.5
  local p = Instance.new("Part")
  p.Name = "Root_"..ri
  p.Size = Vector3.new(rootLen, 0.4 * SCALE, 0.35 * SCALE)
  p.CFrame = CFrame.new(rootX, originY + 0.15, rootZ)
    * CFrame.Angles(0, angle + math.pi/2, 0)
    * CFrame.Angles(-0.15, 0, 0)
  p.Color = Color3.fromRGB(${trunkColorR})
  p.Material = Enum.Material.Wood
  p.Anchored = true
  p.Parent = tree
end
` : ''}

${ttype === 'pine' ? `
-- ── Pine cone layers ─────────────────────────────────────────────────────────
local layers = {
  {y = trunkH * 0.35, r = SCALE * 3.5, h = SCALE * 2.2},
  {y = trunkH * 0.55, r = SCALE * 2.8, h = SCALE * 1.8},
  {y = trunkH * 0.73, r = SCALE * 2.0, h = SCALE * 1.4},
  {y = trunkH * 0.88, r = SCALE * 1.2, h = SCALE * 1.0},
}
for li, layer in ipairs(layers) do
  if ${hasLeaves} then
    local cone = Instance.new("SpecialMesh")
    local part = Instance.new("Part")
    part.Name = "PineLayer_"..li
    part.Size = Vector3.new(layer.r * 2, layer.h, layer.r * 2)
    part.CFrame = CFrame.new(originX, originY + layer.y, originZ)
    part.Color = Color3.fromRGB(${leafColorR})
    part.Material = Enum.Material.LeafyGrass
    part.Anchored = true
    part.Parent = tree
    cone.MeshType = Enum.MeshType.Wedge
    cone.Parent = part
  end
end
` : ttype === 'palm' ? `
-- ── Palm fronds ───────────────────────────────────────────────────────────────
local palmTopY = originY + trunkH
local frondCount = 7
for fi = 1, frondCount do
  local angle = (fi / frondCount) * math.pi * 2
  local frondLen = SCALE * 4.5
  local frondX = originX + math.cos(angle) * frondLen * 0.5
  local frondZ = originZ + math.sin(angle) * frondLen * 0.5
  local frondY = palmTopY + frondLen * 0.1
  local p = Instance.new("Part")
  p.Name = "PalmFrond_"..fi
  p.Size = Vector3.new(frondLen, 0.25 * SCALE, 0.8 * SCALE)
  p.CFrame = CFrame.new(frondX, frondY, frondZ)
    * CFrame.Angles(0, angle + math.pi/2, 0)
    * CFrame.Angles(-0.35, 0, 0)
  p.Color = Color3.fromRGB(${leafColorR})
  p.Material = Enum.Material.LeafyGrass
  p.Anchored = true
  p.Parent = tree
end
` : `
-- ── Branches + leaf clumps (oak/cherry/birch) ────────────────────────────────
local branchStartY = originY + trunkH * 0.55
local numBranches = ${branchCount}
for bi = 1, numBranches do
  local angle = (bi / numBranches) * math.pi * 2
  local bLen = SCALE * (2.0 + math.random() * 1.2)
  local bX = originX + math.cos(angle) * bLen * 0.55
  local bZ = originZ + math.sin(angle) * bLen * 0.55
  local bY = branchStartY + bLen * 0.4

  -- Branch part
  local bp = Instance.new("Part")
  bp.Name = "Branch_"..bi
  bp.Size = Vector3.new(bLen, 0.5 * SCALE, 0.5 * SCALE)
  bp.CFrame = CFrame.new(
    originX + math.cos(angle) * bLen * 0.28,
    branchStartY + bLen * 0.2,
    originZ + math.sin(angle) * bLen * 0.28
  ) * CFrame.Angles(0, angle + math.pi/2, 0) * CFrame.Angles(-0.4, 0, 0)
  bp.Color = Color3.fromRGB(${trunkColorR})
  bp.Material = Enum.Material.Wood
  bp.Anchored = true
  bp.Parent = tree

  -- Leaf clumps at branch tip (3 overlapping spheres for density)
  if ${hasLeaves} then
    for ci = 1, 3 do
      local offX = (math.random() - 0.5) * SCALE * 1.2
      local offY = (math.random() - 0.5) * SCALE * 0.8
      local offZ = (math.random() - 0.5) * SCALE * 1.2
      local clumpSize = SCALE * (2.2 + math.random() * 0.8)
      local useVariant = ci == 2
      sphere(tree, "Leaves_B"..bi.."_C"..ci,
        clumpSize,
        Vector3.new(bX + offX, bY + offY + SCALE, bZ + offZ),
        useVariant and {${leafColorVariantR}} or {${leafColorR}})
    end
  end
end

-- Central canopy mass (main body)
if ${hasLeaves} then
  sphere(tree, "CanopyCenter",
    SCALE * (${ttype === 'oak' ? 5.5 : 4.0}),
    Vector3.new(originX, originY + trunkH * 0.9, originZ),
    {${leafColorR}})
  sphere(tree, "CanopyLeft",
    SCALE * 3.8,
    Vector3.new(originX - SCALE * 1.8, originY + trunkH * 0.85, originZ),
    {${leafColorVariantR}})
  sphere(tree, "CanopyRight",
    SCALE * 3.8,
    Vector3.new(originX + SCALE * 1.8, originY + trunkH * 0.85, originZ),
    {${leafColorR}})
  sphere(tree, "CanopyTop",
    SCALE * 3.0,
    Vector3.new(originX, originY + trunkH * 1.1, originZ),
    {${leafColorVariantR}})
end
`}

${params.withFallenLeaves && season === 'autumn' ? `
-- ── Fallen leaves (scattered around base) ────────────────────────────────────
local fallenColors = {
  {200, 80, 20}, {180, 60, 10}, {210, 110, 30}, {170, 50, 15}, {195, 90, 25}
}
for li = 1, 18 do
  local angle = math.random() * math.pi * 2
  local dist = SCALE * (0.8 + math.random() * 2.5)
  local lx = originX + math.cos(angle) * dist
  local lz = originZ + math.sin(angle) * dist
  local col = fallenColors[(li % #fallenColors) + 1]
  local leaf = Instance.new("Part")
  leaf.Name = "FallenLeaf_"..li
  leaf.Size = Vector3.new(SCALE * 0.6, 0.05, SCALE * 0.5)
  leaf.CFrame = CFrame.new(lx, originY + 0.03, lz)
    * CFrame.Angles(0, math.random() * math.pi * 2, 0)
  leaf.Color = Color3.fromRGB(table.unpack(col))
  leaf.Material = Enum.Material.LeafyGrass
  leaf.Anchored = true
  leaf.CastShadow = false
  leaf.CanCollide = false
  leaf.Parent = tree
end
` : ''}

tree.PrimaryPart = tree:FindFirstChildOfClass("Part")
ChangeHistoryService:SetWaypoint("Built_Tree")
print("[Tree] ${ttype} (${season}, ${tsize}) built.")`
}

