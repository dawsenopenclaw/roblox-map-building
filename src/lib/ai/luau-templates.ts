export interface WeatheringParams {
  targetModelName: string
  type: 'water_staining' | 'rust' | 'moss' | 'age_darkening' | 'dust'
}

export function weatheringEffect(params: WeatheringParams): string {
  const modelName = safe(params.targetModelName)
  const wtype = params.type

  // Per-type color tweak logic as inline Luau
  const colorLogic =
    wtype === 'water_staining' ?
`  -- Water staining: darken toward base, add grey-blue tint
  local t = 1 - math.clamp((p.Position.Y - baseY) / totalHeight, 0, 1)
  local stainStrength = t * 0.45
  local r = math.clamp(c.R - stainStrength * 0.25, 0, 1)
  local g = math.clamp(c.G - stainStrength * 0.15, 0, 1)
  local b = math.clamp(c.B - stainStrength * 0.05 + stainStrength * 0.08, 0, 1)
  p.Color = Color3.new(r, g, b)` :
    wtype === 'rust' ?
`  -- Rust: shift toward brown/orange, stronger on metal parts
  local isMetal = p.Material == Enum.Material.Metal or p.Material == Enum.Material.CorrodedMetal
  local strength = isMetal and 0.6 or 0.25
  local r = math.clamp(c.R * (1 - strength) + 0.72 * strength, 0, 1)
  local g = math.clamp(c.G * (1 - strength) + 0.28 * strength, 0, 1)
  local b = math.clamp(c.B * (1 - strength) + 0.08 * strength, 0, 1)
  p.Color = Color3.new(r, g, b)
  if isMetal then p.Material = Enum.Material.CorrodedMetal end` :
    wtype === 'moss' ?
`  -- Moss: green tint, stronger on top surfaces and lower sections
  local t = 1 - math.clamp((p.Position.Y - baseY) / totalHeight, 0, 1)
  local mossStrength = math.clamp(t * 0.5, 0, 0.55)
  local r = math.clamp(c.R * (1 - mossStrength) + 0.25 * mossStrength, 0, 1)
  local g = math.clamp(c.G * (1 - mossStrength) + 0.45 * mossStrength, 0, 1)
  local b = math.clamp(c.B * (1 - mossStrength) + 0.18 * mossStrength, 0, 1)
  p.Color = Color3.new(r, g, b)` :
    wtype === 'age_darkening' ?
`  -- Age darkening: uniformly darken + desaturate
  local factor = 0.72
  local grey = (c.R + c.G + c.B) / 3
  local r = math.clamp(c.R * factor + grey * 0.05, 0, 1)
  local g = math.clamp(c.G * factor + grey * 0.05, 0, 1)
  local b = math.clamp(c.B * factor + grey * 0.05, 0, 1)
  p.Color = Color3.new(r, g, b)` :
`  -- Dust: shift color toward warm grey, lighter on top
  local t = math.clamp((p.Position.Y - baseY) / totalHeight, 0, 1)
  local dustStrength = t * 0.35 + 0.1
  local dustR, dustG, dustB = 0.82, 0.78, 0.72
  local r = math.clamp(c.R * (1 - dustStrength) + dustR * dustStrength, 0, 1)
  local g = math.clamp(c.G * (1 - dustStrength) + dustG * dustStrength, 0, 1)
  local b = math.clamp(c.B * (1 - dustStrength) + dustB * dustStrength, 0, 1)
  p.Color = Color3.new(r, g, b)`

  return `-- Weathering Effect: ${wtype} → applied to model "${modelName}"
-- Run this in the Command Bar. It modifies part Colors in-place.
-- TIP: Run on a duplicate of your model to preserve the original.
local ChangeHistoryService = game:GetService("ChangeHistoryService")
ChangeHistoryService:SetWaypoint("Before_Weathering_${modelName}")

local model = workspace:FindFirstChild("${modelName}", true)
if not model then
  warn("[Weathering] Model '${modelName}' not found in workspace.")
  return
end

-- Compute vertical bounds for gradient-based effects
local minY, maxY = math.huge, -math.huge
for _, p in ipairs(model:GetDescendants()) do
  if p:IsA("BasePart") then
    minY = math.min(minY, p.Position.Y)
    maxY = math.max(maxY, p.Position.Y)
  end
end
local baseY = minY
local totalHeight = math.max(1, maxY - minY)

local partsModified = 0

for _, p in ipairs(model:GetDescendants()) do
  if p:IsA("BasePart") then
    -- Skip Neon, Glass, and very small detail parts (avoid ruining lights/windows)
    if p.Material == Enum.Material.Neon then continue end
    if p.Material == Enum.Material.Glass then continue end
    if p.Size.Magnitude < 0.4 then continue end

    local c = p.Color
${colorLogic}
    partsModified += 1
  end
end

ChangeHistoryService:SetWaypoint("Applied_Weathering_${modelName}")
print("[Weathering] ${wtype} applied to '" .. model.Name .. "' — " .. partsModified .. " parts modified.")`
}

