export function obbyCheckpoints(params: ObbyCheckpointParams): string {
  const total = Math.max(2, params.totalCheckpoints)
  const reward = params.completionReward

  return `-- Obby Checkpoint System (Script → ServerScriptService)
-- Checkpoints must be Parts named "Checkpoint1", "Checkpoint2", etc. in workspace.Checkpoints
local Players = game:GetService("Players")
local DataStoreService = game:GetService("DataStoreService")

local checkpointStore = DataStoreService:GetDataStore("ObbyCPs_v1")
local TOTAL_CHECKPOINTS = ${total}
${reward ? `local COMPLETION_CURRENCY = "${safe(reward.currency)}"
local COMPLETION_REWARD = ${Math.max(1, reward.amount)}` : ''}

-- In-memory checkpoint progress
local progress: {[number]: number} = {}  -- userId → highest checkpoint reached

local function loadProgress(player: Player)
  local success, data = pcall(function()
    return checkpointStore:GetAsync("cp_" .. player.UserId)
  end)
  progress[player.UserId] = (success and type(data) == "number") and data or 0
end

local function saveProgress(player: Player)
  local userId = player.UserId
  if progress[userId] == nil then return end
  pcall(function()
    checkpointStore:SetAsync("cp_" .. userId, progress[userId])
  end)
end

local function spawnAtCheckpoint(player: Player)
  local cp = progress[player.UserId] or 0
  if cp <= 0 then return end  -- spawn at default start

  local folder = workspace:FindFirstChild("Checkpoints")
  if not folder then return end
  local cpPart = folder:FindFirstChild("Checkpoint" .. cp) :: BasePart?
  if not cpPart then return end

  local char = player.Character
  local hrp = char and char:FindFirstChild("HumanoidRootPart") :: BasePart?
  if hrp then
    hrp.CFrame = cpPart.CFrame * CFrame.new(0, 4, 0)
  end
end

-- Setup touch detectors for all checkpoints
local function setupCheckpoints()
  local folder = workspace:FindFirstChild("Checkpoints")
  if not folder then
    warn("[Obby] No 'Checkpoints' folder found in workspace")
    return
  end

  for i = 1, TOTAL_CHECKPOINTS do
    local cpPart = folder:FindFirstChild("Checkpoint" .. i) :: BasePart?
    if cpPart then
      local detector = Instance.new("TouchTransmitter")
      detector.Parent = cpPart

      cpPart.Touched:Connect(function(hit: BasePart)
        local char = hit:FindFirstAncestorOfClass("Model")
        if not char then return end
        local player = Players:GetPlayerFromCharacter(char)
        if not player then return end

        local current = progress[player.UserId] or 0
        if i <= current then return end  -- already reached this or later

        progress[player.UserId] = i
        saveProgress(player)

        -- Notify client
        local cpEvent = game:GetService("ReplicatedStorage"):FindFirstChild("CheckpointReached")
        if not cpEvent then
          cpEvent = Instance.new("RemoteEvent")
          cpEvent.Name = "CheckpointReached"
          cpEvent.Parent = game:GetService("ReplicatedStorage")
        end
        cpEvent:FireClient(player, i, TOTAL_CHECKPOINTS)

        print("[Obby] " .. player.Name .. " reached checkpoint " .. i)

        ${reward ? `-- Completion reward
        if i == TOTAL_CHECKPOINTS then
          local economyAPI = game:GetService("ServerStorage"):FindFirstChild("EconomyAPI")
          if economyAPI then
            economyAPI:Invoke("add", player, COMPLETION_CURRENCY, COMPLETION_REWARD)
            print("[Obby] " .. player.Name .. " completed obby! Rewarded " .. COMPLETION_REWARD .. " " .. COMPLETION_CURRENCY)
          end
        end` : ''}
      end)
    end
  end
end

Players.PlayerAdded:Connect(function(player: Player)
  loadProgress(player)
  player.CharacterAdded:Connect(function()
    task.wait(0.1)  -- brief delay so character loads
    spawnAtCheckpoint(player)
  end)
end)

Players.PlayerRemoving:Connect(function(player: Player)
  saveProgress(player)
  progress[player.UserId] = nil
end)

task.defer(setupCheckpoints)

print("[Obby] Checkpoint system initialized — ${total} checkpoints")`
}

