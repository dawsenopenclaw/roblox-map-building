export interface SpawnSystemParams {
  spawnPoints: number
  respawnDelay: number
  teamBased?: boolean
}

export function spawnSystem(params: SpawnSystemParams): string {
  const points = Math.max(1, Math.min(20, params.spawnPoints))
  const delay = Math.max(0, params.respawnDelay)

  return `-- Spawn System (Script → ServerScriptService)
local Players = game:GetService("Players")
local Teams = game:GetService("Teams")

local RESPAWN_DELAY = ${delay}
local TEAM_BASED = ${params.teamBased ? 'true' : 'false'}

-- Find all SpawnLocation parts tagged as game spawns
local function getSpawnPoints(): {SpawnLocation}
  local spawns: {SpawnLocation} = {}
  for _, obj in ipairs(workspace:GetDescendants()) do
    if obj:IsA("SpawnLocation") then
      table.insert(spawns, obj)
    end
  end
  return spawns
end

-- Pick a random spawn (or team-specific spawn if team-based)
local function getSpawnForPlayer(player: Player): SpawnLocation?
  local spawns = getSpawnPoints()
  if #spawns == 0 then return nil end

  if TEAM_BASED and player.Team then
    local teamSpawns: {SpawnLocation} = {}
    for _, spawn in ipairs(spawns) do
      if spawn.TeamColor == player.TeamColor then
        table.insert(teamSpawns, spawn)
      end
    end
    if #teamSpawns > 0 then
      return teamSpawns[math.random(#teamSpawns)]
    end
  end

  return spawns[math.random(#spawns)]
end

-- Override default Roblox respawn to use custom spawn logic
local function handleCharacterAdded(player: Player, character: Model)
  local hrp = character:WaitForChild("HumanoidRootPart", 5) :: BasePart?
  if not hrp then return end

  local spawn = getSpawnForPlayer(player)
  if spawn then
    hrp.CFrame = spawn.CFrame * CFrame.new(0, 3, 0)
  end
end

-- Custom respawn with delay
local function handleCharacterRemoving(player: Player)
  if RESPAWN_DELAY > 0 then
    task.delay(RESPAWN_DELAY, function()
      if player and player.Parent then
        player:LoadCharacter()
      end
    end)
  end
end

-- Disable Roblox auto-respawn if we're handling it manually
if RESPAWN_DELAY > 0 then
  game.Players.RespawnTime = RESPAWN_DELAY
end

Players.PlayerAdded:Connect(function(player: Player)
  player.CharacterAdded:Connect(function(character: Model)
    handleCharacterAdded(player, character)
  end)
  if RESPAWN_DELAY > 0 then
    player.CharacterRemoving:Connect(function()
      handleCharacterRemoving(player)
    end)
  end
end)

print("[Spawn] Spawn system ready — ${points} spawn points, ${delay}s respawn delay")`
}

