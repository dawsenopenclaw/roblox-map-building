export interface AnimationSystemParams {
  animationName: string
  animId: string
  speed: number
  looping: boolean
  priority: 'Idle' | 'Movement' | 'Action'
}

export function animationSystem(params: AnimationSystemParams): string {
  const animationName = safe(params.animationName) || 'CustomAnim'
  const animId = safeLuauString(params.animId) || 'rbxassetid://0'
  const speed = Math.max(0.1, Math.min(5.0, params.speed))
  const looping = params.looping !== false
  const priority = ['Idle', 'Movement', 'Action'].includes(params.priority) ? params.priority : 'Action'

  return `-- Animation System: ${animationName} (Server Script → ServerScriptService)
-- Generated by ForjeGames Build Engine
local Players = game:GetService("Players")
local ReplicatedStorage = game:GetService("ReplicatedStorage")
local ChangeHistoryService = game:GetService("ChangeHistoryService")

ChangeHistoryService:SetWaypoint("Before_Animation_${animationName}")

local ANIM_NAME = "${safeLuauString(animationName)}"
local ANIM_ID = "${animId}"
local ANIM_SPEED = ${speed}
local ANIM_LOOPING = ${looping}
local ANIM_PRIORITY = Enum.AnimationPriority.${priority}

-- RemoteEvents for animation control
local animFolder = Instance.new("Folder")
animFolder.Name = "AnimationRemotes"
animFolder.Parent = ReplicatedStorage

local playAnimEvent = Instance.new("RemoteEvent")
playAnimEvent.Name = "PlayAnimation"
playAnimEvent.Parent = animFolder

local stopAnimEvent = Instance.new("RemoteEvent")
stopAnimEvent.Name = "StopAnimation"
stopAnimEvent.Parent = animFolder

-- Track loaded animations per player
local playerAnimations: {[number]: {track: AnimationTrack, animation: Animation}} = {}

-- Create Animation object
local animationObject = Instance.new("Animation")
animationObject.Name = ANIM_NAME
animationObject.AnimationId = ANIM_ID

local function getOrCreateAnimator(humanoid: Humanoid): Animator
  local animator = humanoid:FindFirstChildOfClass("Animator")
  if not animator then
    animator = Instance.new("Animator")
    animator.Parent = humanoid
  end
  return animator
end

local function loadAnimation(player: Player): AnimationTrack?
  local character = player.Character
  if not character then return nil end
  local humanoid = character:FindFirstChildOfClass("Humanoid")
  if not humanoid then return nil end

  local animator = getOrCreateAnimator(humanoid)

  local ok, track = pcall(function()
    return animator:LoadAnimation(animationObject)
  end)

  if not ok or not track then
    warn("[Animation] Failed to load animation for " .. player.Name)
    return nil
  end

  track.Priority = ANIM_PRIORITY
  track.Looped = ANIM_LOOPING

  playerAnimations[player.UserId] = {
    track = track,
    animation = animationObject,
  }

  return track
end

local function playAnimation(player: Player)
  local data = playerAnimations[player.UserId]
  local track: AnimationTrack? = data and data.track or nil

  if not track then
    track = loadAnimation(player)
  end

  if track then
    pcall(function()
      track:Play(0.2, 1, ANIM_SPEED)
    end)
  end
end

local function stopAnimation(player: Player)
  local data = playerAnimations[player.UserId]
  if data and data.track then
    pcall(function()
      data.track:Stop(0.3)
    end)
  end
end

-- Server-triggered animation via RemoteEvent
playAnimEvent.OnServerEvent:Connect(function(player: Player, targetName: string?)
  if targetName then
    -- Play on another player (server authority)
    local targetPlayer = Players:FindFirstChild(targetName)
    if targetPlayer and targetPlayer:IsA("Player") then
      playAnimation(targetPlayer)
    end
  else
    playAnimation(player)
  end
end)

stopAnimEvent.OnServerEvent:Connect(function(player: Player)
  stopAnimation(player)
end)

-- ProximityPrompt trigger option: place a trigger part in Workspace
local function setupProximityTrigger()
  local triggerPart = Instance.new("Part")
  triggerPart.Name = ANIM_NAME .. "_Trigger"
  triggerPart.Size = Vector3.new(4, 4, 4)
  triggerPart.Position = Vector3.new(0, 3, 0)
  triggerPart.Anchored = true
  triggerPart.Transparency = 0.7
  triggerPart.Material = Enum.Material.Neon
  triggerPart.BrickColor = BrickColor.new("Bright violet")
  triggerPart.CanCollide = false
  triggerPart.Parent = game.Workspace

  local prompt = Instance.new("ProximityPrompt")
  prompt.ActionText = "Play " .. ANIM_NAME
  prompt.ObjectText = "Animation"
  prompt.MaxActivationDistance = 10
  prompt.HoldDuration = 0
  prompt.Parent = triggerPart

  prompt.Triggered:Connect(function(player: Player)
    playAnimation(player)
    if not ANIM_LOOPING then
      -- Auto-stop after estimated duration
      task.spawn(function()
        local data = playerAnimations[player.UserId]
        if data and data.track then
          task.wait(data.track.Length / ANIM_SPEED)
        else
          task.wait(2)
        end
      end)
    end
  end)

  -- Stop prompt for looping animations
  if ANIM_LOOPING then
    local stopPrompt = Instance.new("ProximityPrompt")
    stopPrompt.ActionText = "Stop " .. ANIM_NAME
    stopPrompt.ObjectText = "Animation"
    stopPrompt.MaxActivationDistance = 10
    stopPrompt.HoldDuration = 0
    stopPrompt.KeyboardKeyCode = Enum.KeyCode.F
    stopPrompt.Parent = triggerPart

    stopPrompt.Triggered:Connect(function(player: Player)
      stopAnimation(player)
    end)
  end
end

-- Initialize
local ok, err = pcall(function()
  setupProximityTrigger()
end)

if not ok then
  warn("[Animation] Failed to setup: " .. tostring(err))
end

-- Re-load animation when character respawns
Players.PlayerAdded:Connect(function(player: Player)
  player.CharacterAdded:Connect(function()
    task.wait(1)
    playerAnimations[player.UserId] = nil
    loadAnimation(player)
  end)
end)

-- Cleanup on leave
Players.PlayerRemoving:Connect(function(player: Player)
  local data = playerAnimations[player.UserId]
  if data and data.track then
    pcall(function() data.track:Stop() end)
  end
  playerAnimations[player.UserId] = nil
end)

ChangeHistoryService:SetWaypoint("Built_Animation_${animationName}")
print("[Animation] ${animationName} system ready — speed: ${speed}, looping: ${looping}, priority: ${priority}")`
}

