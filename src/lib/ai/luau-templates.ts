export interface DayNightCycleParams {
  cycleDurationMinutes: number
  sunriseHour?: number
  sunsetHour?: number
}

export function dayNightCycle(params: DayNightCycleParams): string {
  const cycleSecs = Math.max(60, params.cycleDurationMinutes * 60)
  const sunriseHour = params.sunriseHour ?? 6
  const sunsetHour = params.sunsetHour ?? 20

  return `-- Day/Night Cycle (Script → ServerScriptService)
local Lighting = game:GetService("Lighting")
local RunService = game:GetService("RunService")

local CYCLE_SECONDS = ${cycleSecs}  -- Real-world seconds for full 24h cycle
local SUNRISE_HOUR = ${sunriseHour}
local SUNSET_HOUR = ${sunsetHour}

-- Initial lighting setup
Lighting.Ambient = Color3.fromRGB(80, 80, 100)
Lighting.OutdoorAmbient = Color3.fromRGB(120, 120, 140)
Lighting.Brightness = 3
Lighting.ShadowSoftness = 0.2
Lighting.FogEnd = 2000
Lighting.FogColor = Color3.fromRGB(180, 190, 200)

-- Atmosphere
local atmosphere = Lighting:FindFirstChildOfClass("Atmosphere") or Instance.new("Atmosphere")
atmosphere.Density = 0.35
atmosphere.Offset = 0.06
atmosphere.Color = Color3.fromRGB(199, 170, 140)
atmosphere.Decay = Color3.fromRGB(106, 127, 153)
atmosphere.Glare = 0.06
atmosphere.Haze = 2.5
atmosphere.Parent = Lighting

-- Color-correct the sky colors by time of day
local function setTimeColors(hour: number)
  local isDay = hour >= SUNRISE_HOUR and hour < SUNSET_HOUR
  local isSunrise = hour >= SUNRISE_HOUR and hour < (SUNRISE_HOUR + 2)
  local isSunset = hour >= (SUNSET_HOUR - 2) and hour < SUNSET_HOUR

  if isSunrise then
    atmosphere.Color = Color3.fromRGB(255, 160, 90)
    Lighting.Ambient = Color3.fromRGB(100, 80, 70)
    Lighting.Brightness = 1.5
  elseif isSunset then
    atmosphere.Color = Color3.fromRGB(255, 120, 60)
    Lighting.Ambient = Color3.fromRGB(100, 70, 60)
    Lighting.Brightness = 1.2
  elseif isDay then
    atmosphere.Color = Color3.fromRGB(199, 170, 140)
    Lighting.Ambient = Color3.fromRGB(80, 80, 100)
    Lighting.Brightness = 3
  else
    -- Night
    atmosphere.Color = Color3.fromRGB(10, 10, 30)
    Lighting.Ambient = Color3.fromRGB(20, 20, 40)
    Lighting.Brightness = 0.3
  end
end

-- Advance time every frame
RunService.Heartbeat:Connect(function(dt: number)
  local currentHour = Lighting.ClockTime
  local hoursPerSecond = 24 / CYCLE_SECONDS
  local newHour = (currentHour + dt * hoursPerSecond) % 24
  Lighting.ClockTime = newHour
  setTimeColors(newHour)
end)

-- Sync time event for clients that need it
local timeEvent = Instance.new("RemoteEvent")
timeEvent.Name = "TimeSync"
timeEvent.Parent = game:GetService("ReplicatedStorage")

-- Broadcast time every 5 seconds
local lastSync = 0
RunService.Heartbeat:Connect(function()
  if tick() - lastSync >= 5 then
    lastSync = tick()
    timeEvent:FireAllClients(Lighting.ClockTime)
  end
end)

print("[Day/Night] Cycle active — ${params.cycleDurationMinutes}min real-time cycle")`
}

// ─────────────────────────────────────────────────────────────────────────────
// 8. SPAWN SYSTEM
// ─────────────────────────────────────────────────────────────────────────────

export interface SpawnSystemParams {
  spawnPoints: number
  respawnDelay: number
  teamBased?: boolean
}

export function spawnSystem(params: SpawnSystemParams): string {
  const points = Math.max(1, Math.min(20, params.spawnPoints))
  const delay = Math.max(0, params.respawnDelay)

  return `-- Spawn System (Script → ServerScriptService)
local Players = game:GetService("Players")
local Teams = game:GetService("Teams")

local RESPAWN_DELAY = ${delay}
local TEAM_BASED = ${params.teamBased ? 'true' : 'false'}

-- Find all SpawnLocation parts tagged as game spawns
local function getSpawnPoints(): {SpawnLocation}
  local spawns: {SpawnLocation} = {}
  for _, obj in ipairs(workspace:GetDescendants()) do
    if obj:IsA("SpawnLocation") then
      table.insert(spawns, obj)
    end
  end
  return spawns
end

-- Pick a random spawn (or team-specific spawn if team-based)
local function getSpawnForPlayer(player: Player): SpawnLocation?
  local spawns = getSpawnPoints()
  if #spawns == 0 then return nil end

  if TEAM_BASED and player.Team then
    local teamSpawns: {SpawnLocation} = {}
    for _, spawn in ipairs(spawns) do
      if spawn.TeamColor == player.TeamColor then
        table.insert(teamSpawns, spawn)
      end
    end
    if #teamSpawns > 0 then
      return teamSpawns[math.random(#teamSpawns)]
    end
  end

  return spawns[math.random(#spawns)]
end

-- Override default Roblox respawn to use custom spawn logic
local function handleCharacterAdded(player: Player, character: Model)
  local hrp = character:WaitForChild("HumanoidRootPart", 5) :: BasePart?
  if not hrp then return end

  local spawn = getSpawnForPlayer(player)
  if spawn then
    hrp.CFrame = spawn.CFrame * CFrame.new(0, 3, 0)
  end
end

-- Custom respawn with delay
local function handleCharacterRemoving(player: Player)
  if RESPAWN_DELAY > 0 then
    task.delay(RESPAWN_DELAY, function()
      if player and player.Parent then
        player:LoadCharacter()
      end
    end)
  end
end

-- Disable Roblox auto-respawn if we're handling it manually
if RESPAWN_DELAY > 0 then
  game.Players.RespawnTime = RESPAWN_DELAY
end

Players.PlayerAdded:Connect(function(player: Player)
  player.CharacterAdded:Connect(function(character: Model)
    handleCharacterAdded(player, character)
  end)
  if RESPAWN_DELAY > 0 then
    player.CharacterRemoving:Connect(function()
      handleCharacterRemoving(player)
    end)
  end
end)

print("[Spawn] Spawn system ready — ${points} spawn points, ${delay}s respawn delay")`
}

// ─────────────────────────────────────────────────────────────────────────────
// 9. OBBY CHECKPOINT SYSTEM
// ─────────────────────────────────────────────────────────────────────────────

export interface ObbyCheckpointParams {
  totalCheckpoints: number
  completionReward?: { currency: string; amount: number }
}

export function obbyCheckpoints(params: ObbyCheckpointParams): string {
  const total = Math.max(2, params.totalCheckpoints)
  const reward = params.completionReward

  return `-- Obby Checkpoint System (Script → ServerScriptService)
-- Checkpoints must be Parts named "Checkpoint1", "Checkpoint2", etc. in workspace.Checkpoints
local Players = game:GetService("Players")
local DataStoreService = game:GetService("DataStoreService")

local checkpointStore = DataStoreService:GetDataStore("ObbyCPs_v1")
local TOTAL_CHECKPOINTS = ${total}
${reward ? `local COMPLETION_CURRENCY = "${safe(reward.currency)}"
local COMPLETION_REWARD = ${Math.max(1, reward.amount)}` : ''}

-- In-memory checkpoint progress
local progress: {[number]: number} = {}  -- userId → highest checkpoint reached

local function loadProgress(player: Player)
  local success, data = pcall(function()
    return checkpointStore:GetAsync("cp_" .. player.UserId)
  end)
  progress[player.UserId] = (success and type(data) == "number") and data or 0
end

local function saveProgress(player: Player)
  local userId = player.UserId
  if progress[userId] == nil then return end
  pcall(function()
    checkpointStore:SetAsync("cp_" .. userId, progress[userId])
  end)
end

local function spawnAtCheckpoint(player: Player)
  local cp = progress[player.UserId] or 0
  if cp <= 0 then return end  -- spawn at default start

  local folder = workspace:FindFirstChild("Checkpoints")
  if not folder then return end
  local cpPart = folder:FindFirstChild("Checkpoint" .. cp) :: BasePart?
  if not cpPart then return end

  local char = player.Character
  local hrp = char and char:FindFirstChild("HumanoidRootPart") :: BasePart?
  if hrp then
    hrp.CFrame = cpPart.CFrame * CFrame.new(0, 4, 0)
  end
end

-- Setup touch detectors for all checkpoints
local function setupCheckpoints()
  local folder = workspace:FindFirstChild("Checkpoints")
  if not folder then
    warn("[Obby] No 'Checkpoints' folder found in workspace")
    return
  end

  for i = 1, TOTAL_CHECKPOINTS do
    local cpPart = folder:FindFirstChild("Checkpoint" .. i) :: BasePart?
    if cpPart then
      local detector = Instance.new("TouchTransmitter")
      detector.Parent = cpPart

      cpPart.Touched:Connect(function(hit: BasePart)
        local char = hit:FindFirstAncestorOfClass("Model")
        if not char then return end
        local player = Players:GetPlayerFromCharacter(char)
        if not player then return end

        local current = progress[player.UserId] or 0
        if i <= current then return end  -- already reached this or later

        progress[player.UserId] = i
        saveProgress(player)

        -- Notify client
        local cpEvent = game:GetService("ReplicatedStorage"):FindFirstChild("CheckpointReached")
        if not cpEvent then
          cpEvent = Instance.new("RemoteEvent")
          cpEvent.Name = "CheckpointReached"
          cpEvent.Parent = game:GetService("ReplicatedStorage")
        end
        cpEvent:FireClient(player, i, TOTAL_CHECKPOINTS)

        print("[Obby] " .. player.Name .. " reached checkpoint " .. i)

        ${reward ? `-- Completion reward
        if i == TOTAL_CHECKPOINTS then
          local economyAPI = game:GetService("ServerStorage"):FindFirstChild("EconomyAPI")
          if economyAPI then
            economyAPI:Invoke("add", player, COMPLETION_CURRENCY, COMPLETION_REWARD)
            print("[Obby] " .. player.Name .. " completed obby! Rewarded " .. COMPLETION_REWARD .. " " .. COMPLETION_CURRENCY)
          end
        end` : ''}
      end)
    end
  end
end

Players.PlayerAdded:Connect(function(player: Player)
  loadProgress(player)
  player.CharacterAdded:Connect(function()
    task.wait(0.1)  -- brief delay so character loads
    spawnAtCheckpoint(player)
  end)
end)

Players.PlayerRemoving:Connect(function(player: Player)
  saveProgress(player)
  progress[player.UserId] = nil
end)

task.defer(setupCheckpoints)

print("[Obby] Checkpoint system initialized — ${total} checkpoints")`
}

