// ── Sanitisation helper ───────────────────────────────────────────────────────

function safe(val: string): string {
  return String(val).replace(/[^a-zA-Z0-9_ ]/g, '').slice(0, 64)
}

function safeLuauString(val: string): string {
  return String(val).replace(/\\/g, '\\\\').replace(/"/g, '\\"').slice(0, 200)
}

// ─────────────────────────────────────────────────────────────────────────────
// 1. ECONOMY SYSTEM — currency, wallet, leaderstat, server-authoritative shop
// ─────────────────────────────────────────────────────────────────────────────

export interface EconomySystemParams {
  currencies: string[]
  startingCash: number
  shopItems?: Array<{ name: string; price: number }>
}

export function economySystem(params: EconomySystemParams): string {
  const currencies = params.currencies.map(safe)
  const startingCash = Math.max(0, Math.floor(params.startingCash))
  const primaryCurrency = currencies[0] ?? 'Cash'

  const shopItemEntries = (params.shopItems ?? [])
    .map((item) => `  ["${safeLuauString(item.name)}"] = ${Math.max(1, item.price)},`)
    .join('\n')

  return `-- Economy System (Server Script → ServerScriptService)
-- Generated by ForjeGames Build Engine
local Players = game:GetService("Players")
local DataStoreService = game:GetService("DataStoreService")

local economyStore = DataStoreService:GetDataStore("EconomyStore_v1")

local STARTING_BALANCES: {[string]: number} = {
${currencies.map((c, i) => `  ["${c}"] = ${i === 0 ? startingCash : 0},`).join('\n')}
}

local SHOP_PRICES: {[string]: number} = {
${shopItemEntries || '  -- No shop items defined'}
}

-- Wallet: in-memory balances per player
local wallets: {[number]: {[string]: number}} = {}

local function loadWallet(player: Player)
  local userId = player.UserId
  local success, data = pcall(function()
    return economyStore:GetAsync("player_" .. userId)
  end)
  local saved: {[string]: number} = (success and type(data) == "table" and data) or {}

  wallets[userId] = {}
  for currency, startAmount in STARTING_BALANCES do
    wallets[userId][currency] = saved[currency] or startAmount
  end
end

local function saveWallet(player: Player)
  local userId = player.UserId
  if not wallets[userId] then return end
  pcall(function()
    economyStore:SetAsync("player_" .. userId, wallets[userId])
  end)
end

local function getBalance(player: Player, currency: string): number
  local userId = player.UserId
  if not wallets[userId] then return 0 end
  return wallets[userId][currency] or 0
end

local function addCurrency(player: Player, currency: string, amount: number): boolean
  if amount <= 0 then return false end
  local userId = player.UserId
  if not wallets[userId] then return false end
  wallets[userId][currency] = (wallets[userId][currency] or 0) + amount
  -- Update leaderstat
  local ls = player:FindFirstChild("leaderstats")
  if ls then
    local stat = ls:FindFirstChild(currency)
    if stat then stat.Value = wallets[userId][currency] end
  end
  return true
end

local function spendCurrency(player: Player, currency: string, amount: number): boolean
  if amount <= 0 then return false end
  local userId = player.UserId
  if not wallets[userId] then return false end
  local current = wallets[userId][currency] or 0
  if current < amount then return false end
  wallets[userId][currency] = current - amount
  local ls = player:FindFirstChild("leaderstats")
  if ls then
    local stat = ls:FindFirstChild(currency)
    if stat then stat.Value = wallets[userId][currency] end
  end
  return true
end

local function setupLeaderstats(player: Player)
  local leaderstats = Instance.new("Folder")
  leaderstats.Name = "leaderstats"
  leaderstats.Parent = player

  for _, currency in ipairs({"${currencies.join('", "')}"}) do
    local stat = Instance.new("IntValue")
    stat.Name = currency
    stat.Value = wallets[player.UserId][currency] or 0
    stat.Parent = leaderstats
  end
end

-- Shop purchase handler (RemoteFunction — server validates all purchases)
local purchaseRemote = Instance.new("RemoteFunction")
purchaseRemote.Name = "PurchaseItem"
purchaseRemote.Parent = game:GetService("ReplicatedStorage")

purchaseRemote.OnServerInvoke = function(player: Player, itemName: string): (boolean, string)
  if type(itemName) ~= "string" then return false, "Invalid item" end
  local price = SHOP_PRICES[itemName]
  if not price then return false, "Item not found" end
  if not spendCurrency(player, "${primaryCurrency}", price) then
    return false, "Insufficient ${primaryCurrency}"
  end
  return true, "Purchase successful"
end

-- Player lifecycle
Players.PlayerAdded:Connect(function(player: Player)
  loadWallet(player)
  setupLeaderstats(player)
end)

Players.PlayerRemoving:Connect(function(player: Player)
  saveWallet(player)
  wallets[player.UserId] = nil
end)

-- Public API (module-style exposure via BindableFunction for other scripts)
local economyAPI = Instance.new("BindableFunction")
economyAPI.Name = "EconomyAPI"
economyAPI.Parent = game:GetService("ServerStorage")

economyAPI.OnInvoke = function(action: string, player: Player, currency: string, amount: number)
  if action == "get" then return getBalance(player, currency)
  elseif action == "add" then return addCurrency(player, currency, amount)
  elseif action == "spend" then return spendCurrency(player, currency, amount)
  end
end

print("[Economy] System initialized with currencies: ${currencies.join(', ')}")`
}

// ─────────────────────────────────────────────────────────────────────────────
// 2. TYCOON DROPPER / COLLECTOR / UPGRADER
// ─────────────────────────────────────────────────────────────────────────────

export interface TycoonDropperParams {
  resourceName: string
  value: number
  interval: number
  currency?: string
}

export function tycoonDropper(params: TycoonDropperParams): string {
  const resource = safe(params.resourceName)
  const value = Math.max(1, Math.floor(params.value))
  const interval = Math.max(0.5, params.interval)
  const currency = safe(params.currency ?? 'Cash')

  return `-- Tycoon Dropper System (Script inside dropper Model in workspace)
-- Resource: ${resource} | Value: ${value} ${currency} | Interval: ${interval}s
local RunService = game:GetService("RunService")
local TweenService = game:GetService("TweenService")

local dropper = script.Parent -- Model containing the dropper part
local dropPart = dropper:FindFirstChild("DropPart") or dropper.PrimaryPart
local collector = dropper.Parent:FindFirstChild("Collector") -- sibling Collector model

assert(dropPart, "[Dropper] No DropPart found in " .. dropper.Name)

local RESOURCE_NAME = "${resource}"
local RESOURCE_VALUE = ${value}
local DROP_INTERVAL = ${interval}
local CURRENCY = "${currency}"

-- Find which player owns this tycoon
local function getTycoonOwner(): Player?
  local tycoon = dropper:FindFirstAncestorOfClass("Model")
  if not tycoon then return nil end
  local ownerValue = tycoon:FindFirstChild("Owner")
  if not ownerValue or ownerValue.ClassName ~= "ObjectValue" then return nil end
  return ownerValue.Value :: Player?
end

local function dropResource()
  local owner = getTycoonOwner()
  if not owner then return end

  -- Create the resource ball
  local ball = Instance.new("Part")
  ball.Name = RESOURCE_NAME
  ball.Size = Vector3.new(1, 1, 1)
  ball.Shape = Enum.PartType.Ball
  ball.Material = Enum.Material.SmoothPlastic
  ball.Color = Color3.fromRGB(255, 215, 0) -- gold
  ball.CastShadow = false
  ball.Position = dropPart.Position + Vector3.new(0, 2, 0)
  ball.Parent = workspace

  -- Add value tag for collection
  local val = Instance.new("IntValue")
  val.Name = "ResourceValue"
  val.Value = RESOURCE_VALUE
  val.Parent = ball

  local ownerTag = Instance.new("StringValue")
  ownerTag.Name = "OwnerUserId"
  ownerTag.Value = tostring(owner.UserId)
  ownerTag.Parent = ball

  -- Auto-destroy after 30 seconds if not collected
  game:GetService("Debris"):AddItem(ball, 30)
end

-- Collector touch detection (server-side, authoritative)
if collector then
  local collectPart = collector:FindFirstChildOfClass("Part") or collector.PrimaryPart
  if collectPart then
    collectPart.Touched:Connect(function(hit: BasePart)
      local ball = hit
      if ball.Name ~= RESOURCE_NAME then return end
      local ownerTag = ball:FindFirstChild("OwnerUserId")
      if not ownerTag then return end

      local owner = getTycoonOwner()
      if not owner or tostring(owner.UserId) ~= ownerTag.Value then return end

      local valueTag = ball:FindFirstChild("ResourceValue")
      local amount = valueTag and valueTag.Value or RESOURCE_VALUE

      ball:Destroy()

      -- Credit the player via EconomyAPI
      local economyAPI = game:GetService("ServerStorage"):FindFirstChild("EconomyAPI")
      if economyAPI then
        economyAPI:Invoke("add", owner, CURRENCY, amount)
      end
    end)
  end
end

-- Drop loop
local lastDrop = 0
RunService.Heartbeat:Connect(function()
  if tick() - lastDrop >= DROP_INTERVAL then
    lastDrop = tick()
    dropResource()
  end
end)

print("[Dropper] ${resource} dropper active — " .. DROP_INTERVAL .. "s interval, +" .. RESOURCE_VALUE .. " " .. CURRENCY)`
}

// ─────────────────────────────────────────────────────────────────────────────
// 3. NPC DIALOG SYSTEM
// ─────────────────────────────────────────────────────────────────────────────

export interface NpcDialogParams {
  npcName: string
  dialogLines: string[]
  questName?: string
  questRewardCurrency?: string
  questRewardAmount?: number
}

export function npcDialogSystem(params: NpcDialogParams): string {
  const npcName = safe(params.npcName)
  const lines = params.dialogLines.map((l) => `  "${safeLuauString(l)}"`)
  const questName = params.questName ? safe(params.questName) : null
  const rewardCurrency = safe(params.questRewardCurrency ?? 'Cash')
  const rewardAmount = Math.max(0, Math.floor(params.questRewardAmount ?? 0))

  return `-- NPC Dialog System — Script inside NPC Model (workspace)
-- NPC: ${npcName} | Lines: ${params.dialogLines.length} | Quest: ${questName ?? 'None'}
local Players = game:GetService("Players")
local TweenService = game:GetService("TweenService")

local npc = script.Parent
local rootPart = npc:FindFirstChild("HumanoidRootPart") or npc.PrimaryPart
local PROXIMITY_RANGE = 12 -- studs

assert(rootPart, "[NPC Dialog] No HumanoidRootPart in " .. npc.Name)

local DIALOG_LINES: {string} = {
${lines.join(',\n')}
}

${questName ? `local QUEST_NAME = "${questName}"
local QUEST_REWARD_CURRENCY = "${rewardCurrency}"
local QUEST_REWARD_AMOUNT = ${rewardAmount}` : ''}

-- Billboard for proximity hint
local billboard = Instance.new("BillboardGui")
billboard.Name = "DialogHint"
billboard.Size = UDim2.new(0, 200, 0, 50)
billboard.StudsOffset = Vector3.new(0, 3, 0)
billboard.AlwaysOnTop = false
billboard.Enabled = false
billboard.Parent = rootPart

local hintLabel = Instance.new("TextLabel")
hintLabel.Size = UDim2.fromScale(1, 1)
hintLabel.BackgroundColor3 = Color3.fromRGB(20, 20, 20)
hintLabel.BackgroundTransparency = 0.3
hintLabel.TextColor3 = Color3.fromRGB(255, 215, 0)
hintLabel.Font = Enum.Font.GothamBold
hintLabel.TextSize = 14
hintLabel.Text = "Press E to talk to ${npcName}"
hintLabel.Parent = billboard

local UICorner = Instance.new("UICorner")
UICorner.CornerRadius = UDim.new(0, 8)
UICorner.Parent = hintLabel

-- Track which players are in range
local playersInRange: {[number]: boolean} = {}
local dialogState: {[number]: number} = {}

local function showDialog(player: Player, lineIndex: number)
  local line = DIALOG_LINES[lineIndex]
  if not line then return end

  -- Fire to client via RemoteEvent (dialog display handled client-side)
  local dialogEvent = game:GetService("ReplicatedStorage"):FindFirstChild("ShowDialog")
  if not dialogEvent then
    dialogEvent = Instance.new("RemoteEvent")
    dialogEvent.Name = "ShowDialog"
    dialogEvent.Parent = game:GetService("ReplicatedStorage")
  end
  dialogEvent:FireClient(player, {
    npcName = "${npcName}",
    line = line,
    lineIndex = lineIndex,
    totalLines = #DIALOG_LINES,
    hasQuest = ${questName ? 'true' : 'false'}
  })
end

local function advanceDialog(player: Player)
  local userId = player.UserId
  local current = dialogState[userId] or 0
  current = current + 1

  if current > #DIALOG_LINES then
    -- Dialog complete
    dialogState[userId] = 0
    ${questName ? `-- Grant quest reward
    local economyAPI = game:GetService("ServerStorage"):FindFirstChild("EconomyAPI")
    if economyAPI then
      economyAPI:Invoke("add", player, QUEST_REWARD_CURRENCY, QUEST_REWARD_AMOUNT)
    end
    print("[NPC] Quest '${questName}' completed for " .. player.Name)` : ''}
    return
  end

  dialogState[userId] = current
  showDialog(player, current)
end

-- Proximity detection loop
local heartbeat = game:GetService("RunService").Heartbeat
heartbeat:Connect(function()
  if not rootPart then return end
  for _, player in ipairs(Players:GetPlayers()) do
    local char = player.Character
    if not char then continue end
    local hrp = char:FindFirstChild("HumanoidRootPart")
    if not hrp then continue end
    local dist = (hrp.Position - rootPart.Position).Magnitude
    local inRange = dist <= PROXIMITY_RANGE
    local wasInRange = playersInRange[player.UserId]

    if inRange ~= wasInRange then
      playersInRange[player.UserId] = inRange
      billboard.Enabled = inRange
    end
  end
end)

-- Talk input (E key) — handled via RemoteEvent from client
local talkEvent = game:GetService("ReplicatedStorage"):FindFirstChild("TalkToNPC")
if not talkEvent then
  talkEvent = Instance.new("RemoteEvent")
  talkEvent.Name = "TalkToNPC"
  talkEvent.Parent = game:GetService("ReplicatedStorage")
end

talkEvent.OnServerEvent:Connect(function(player: Player, targetNpcName: string)
  if targetNpcName ~= "${npcName}" then return end
  if not playersInRange[player.UserId] then return end
  advanceDialog(player)
end)

print("[NPC Dialog] ${npcName} initialized with " .. #DIALOG_LINES .. " dialog lines")`
}

// ─────────────────────────────────────────────────────────────────────────────
// 4. LEADERBOARD / PROGRESSION SYSTEM
// ─────────────────────────────────────────────────────────────────────────────

export interface LeaderboardParams {
  stats: Array<{ name: string; startValue: number; isXP?: boolean }>
  xpToLevelCurve?: 'linear' | 'quadratic' | 'exponential'
  maxLevel?: number
}

export function leaderboardProgression(params: LeaderboardParams): string {
  const stats = params.stats.map((s) => ({ ...s, name: safe(s.name) }))
  const maxLevel = Math.max(10, params.maxLevel ?? 100)
  const curve = params.xpToLevelCurve ?? 'quadratic'
  const xpStat = stats.find((s) => s.isXP)

  const levelFormula =
    curve === 'linear'
      ? `return level * 100`
      : curve === 'exponential'
        ? `return math.floor(100 * (1.5 ^ (level - 1)))`
        : `return math.floor(100 * (level ^ 1.8))`

  return `-- Leaderboard & Progression System (ServerScript)
local Players = game:GetService("Players")
local DataStoreService = game:GetService("DataStoreService")

local statsStore = DataStoreService:GetDataStore("PlayerStats_v1")
local MAX_LEVEL = ${maxLevel}

local DEFAULT_STATS: {[string]: number} = {
${stats.map((s) => `  ["${s.name}"] = ${s.startValue},`).join('\n')}
}

-- XP-to-level curve: ${curve}
local function xpForLevel(level: number): number
  ${levelFormula}
end

local playerData: {[number]: {[string]: number}} = {}

local function loadStats(player: Player)
  local userId = player.UserId
  local success, data = pcall(function()
    return statsStore:GetAsync("stats_" .. userId)
  end)
  local saved: {[string]: number} = (success and type(data) == "table" and data) or {}
  playerData[userId] = {}
  for stat, defaultVal in DEFAULT_STATS do
    playerData[userId][stat] = saved[stat] or defaultVal
  end
end

local function saveStats(player: Player)
  local userId = player.UserId
  if not playerData[userId] then return end
  pcall(function()
    statsStore:SetAsync("stats_" .. userId, playerData[userId])
  end)
end

local function setupLeaderstats(player: Player)
  -- Remove existing leaderstats if present (economy system may have added them)
  local existing = player:FindFirstChild("leaderstats")
  if existing then existing:Destroy() end

  local leaderstats = Instance.new("Folder")
  leaderstats.Name = "leaderstats"
  leaderstats.Parent = player

  for _, stat in ipairs({${stats.map((s) => `"${s.name}"`).join(', ')}}) do
    local val = Instance.new("IntValue")
    val.Name = stat
    val.Value = playerData[player.UserId][stat] or DEFAULT_STATS[stat] or 0
    val.Parent = leaderstats
  end
end

${xpStat ? `-- Level-up handler (triggered when XP changes)
local function checkLevelUp(player: Player)
  local userId = player.UserId
  local data = playerData[userId]
  if not data then return end

  local currentXP = data["${xpStat.name}"] or 0
  local currentLevel = data["Level"] or 1
  if currentLevel >= MAX_LEVEL then return end

  while currentLevel < MAX_LEVEL and currentXP >= xpForLevel(currentLevel) do
    currentXP -= xpForLevel(currentLevel)
    currentLevel += 1
    data["${xpStat.name}"] = currentXP
    data["Level"] = currentLevel
    -- Notify client
    local levelUpEvent = game:GetService("ReplicatedStorage"):FindFirstChild("LevelUp")
    if not levelUpEvent then
      levelUpEvent = Instance.new("RemoteEvent")
      levelUpEvent.Name = "LevelUp"
      levelUpEvent.Parent = game:GetService("ReplicatedStorage")
    end
    levelUpEvent:FireClient(player, currentLevel)
    print("[Progression] " .. player.Name .. " reached Level " .. currentLevel)
  end

  -- Sync leaderstat
  local ls = player:FindFirstChild("leaderstats")
  if ls then
    local xpStat = ls:FindFirstChild("${xpStat.name}")
    if xpStat then xpStat.Value = data["${xpStat.name}"] or 0 end
    local levelStat = ls:FindFirstChild("Level")
    if levelStat then levelStat.Value = currentLevel end
  end
end` : ''}

-- BindableFunction API for other server scripts to update stats
local statsAPI = Instance.new("BindableFunction")
statsAPI.Name = "StatsAPI"
statsAPI.Parent = game:GetService("ServerStorage")

statsAPI.OnInvoke = function(action: string, player: Player, statName: string, amount: number): number
  local userId = player.UserId
  if not playerData[userId] then return 0 end

  if action == "get" then
    return playerData[userId][statName] or 0
  elseif action == "set" then
    playerData[userId][statName] = amount
  elseif action == "add" then
    playerData[userId][statName] = (playerData[userId][statName] or 0) + amount
    ${xpStat ? `if statName == "${xpStat.name}" then checkLevelUp(player) end` : ''}
  end

  -- Sync to leaderstats
  local ls = player:FindFirstChild("leaderstats")
  if ls then
    local stat = ls:FindFirstChild(statName)
    if stat then stat.Value = playerData[userId][statName] or 0 end
  end

  return playerData[userId][statName] or 0
end

Players.PlayerAdded:Connect(function(player)
  loadStats(player)
  setupLeaderstats(player)
end)

Players.PlayerRemoving:Connect(function(player)
  saveStats(player)
  playerData[player.UserId] = nil
end)

print("[Progression] Leaderboard system initialized — ${stats.map((s) => s.name).join(', ')}")`
}

// ─────────────────────────────────────────────────────────────────────────────
// 5. BASIC CLICK COMBAT
// ─────────────────────────────────────────────────────────────────────────────

export interface CombatParams {
  clickDamage: number
  attackRange: number
  attackCooldown: number
  enemyTag?: string
}

export function basicCombat(params: CombatParams): string {
  const damage = Math.max(1, Math.floor(params.clickDamage))
  const range = Math.max(5, params.attackRange)
  const cooldown = Math.max(0.1, params.attackCooldown)
  const enemyTag = safe(params.enemyTag ?? 'Enemy')

  return `-- Basic Click Combat System (LocalScript → StarterPlayerScripts)
local Players = game:GetService("Players")
local UserInputService = game:GetService("UserInputService")
local RunService = game:GetService("RunService")

local player = Players.LocalPlayer
local mouse = player:GetMouse()
local camera = workspace.CurrentCamera

local CLICK_DAMAGE = ${damage}
local ATTACK_RANGE = ${range}
local ATTACK_COOLDOWN = ${cooldown}
local ENEMY_TAG = "${enemyTag}"

local lastAttack = 0
local attackRemote: RemoteEvent

-- Wait for combat remote to be created by server
local RS = game:GetService("ReplicatedStorage")
local combatRemote = RS:WaitForChild("CombatAttack", 10) :: RemoteEvent?
if not combatRemote then
  warn("[Combat] CombatAttack RemoteEvent not found in ReplicatedStorage")
  return
end

-- Visual feedback: swing animation
local function playSwingEffect(hitPosition: Vector3)
  local effect = Instance.new("Part")
  effect.Anchored = true
  effect.CanCollide = false
  effect.CastShadow = false
  effect.Size = Vector3.new(0.5, 0.5, 0.5)
  effect.Shape = Enum.PartType.Ball
  effect.Material = Enum.Material.Neon
  effect.Color = Color3.fromRGB(255, 80, 80)
  effect.Position = hitPosition
  effect.Parent = workspace
  game:GetService("Debris"):AddItem(effect, 0.15)
end

mouse.Button1Down:Connect(function()
  local now = tick()
  if now - lastAttack < ATTACK_COOLDOWN then return end

  local char = player.Character
  if not char then return end
  local hrp = char:FindFirstChild("HumanoidRootPart") :: BasePart?
  if not hrp then return end

  -- Raycast from camera through mouse
  local unitRay = camera:ScreenPointToRay(mouse.X, mouse.Y)
  local rayParams = RaycastParams.new()
  rayParams.FilterDescendantsInstances = {char}
  rayParams.FilterType = Enum.RaycastFilterType.Exclude

  local result = workspace:Raycast(unitRay.Origin, unitRay.Direction * ATTACK_RANGE, rayParams)
  if not result then return end

  local hitPart = result.Instance
  if not hitPart then return end

  -- Check if hit part belongs to an enemy model
  local model = hitPart:FindFirstAncestorOfClass("Model")
  if not model then return end

  local humanoid = model:FindFirstChildOfClass("Humanoid")
  if not humanoid then return end

  -- Validate range (server will also validate — this is just client feedback)
  if (hrp.Position - result.Position).Magnitude > ATTACK_RANGE then return end

  lastAttack = now
  playSwingEffect(result.Position)
  combatRemote:FireServer(model, CLICK_DAMAGE)
end)

-- Server combat handler (place in ServerScriptService)
-- Note: This section below should be in a SEPARATE ServerScript
--[[
local combatRemote = Instance.new("RemoteEvent")
combatRemote.Name = "CombatAttack"
combatRemote.Parent = game:GetService("ReplicatedStorage")

local attackCooldowns: {[number]: number} = {}

combatRemote.OnServerEvent:Connect(function(player: Player, targetModel: Model, damage: number)
  -- Server-side validation
  local userId = player.UserId
  local now = tick()
  if now - (attackCooldowns[userId] or 0) < ${cooldown * 0.9} then return end
  attackCooldowns[userId] = now

  -- Validate target is a real enemy with humanoid
  local humanoid = targetModel and targetModel:FindFirstChildOfClass("Humanoid")
  if not humanoid or humanoid.Health <= 0 then return end

  -- Validate range
  local char = player.Character
  local hrp = char and char:FindFirstChild("HumanoidRootPart")
  local targetHrp = targetModel:FindFirstChild("HumanoidRootPart")
  if not hrp or not targetHrp then return end
  if (hrp.Position - targetHrp.Position).Magnitude > ${range + 5} then return end

  -- Apply damage (always between 1 and 2x declared damage to prevent exploit)
  local safeDamage = math.clamp(damage, 1, ${damage * 2})
  humanoid:TakeDamage(safeDamage)
end)
--]]

print("[Combat] Click combat initialized — ${damage} dmg, ${range} studs range, ${cooldown}s cooldown")`
}

// ─────────────────────────────────────────────────────────────────────────────
// 6. INVENTORY SYSTEM
// ─────────────────────────────────────────────────────────────────────────────

export interface InventoryParams {
  maxSlots: number
  categories: string[]
  stackable?: boolean
}

export function inventorySystem(params: InventoryParams): string {
  const maxSlots = Math.max(1, Math.min(100, params.maxSlots))
  const categories = params.categories.map(safe)

  return `-- Inventory System (ServerScript → ServerScriptService)
local Players = game:GetService("Players")
local DataStoreService = game:GetService("DataStoreService")

local inventoryStore = DataStoreService:GetDataStore("Inventory_v1")
local MAX_SLOTS = ${maxSlots}
local CATEGORIES = {"${categories.join('", "')}"}
local STACKABLE = ${params.stackable ? 'true' : 'false'}

export type InventoryItem = {
  id: string,
  name: string,
  category: string,
  quantity: number,
  metadata: {[string]: any}?,
}

local inventories: {[number]: {[number]: InventoryItem?}} = {}

local function loadInventory(player: Player)
  local userId = player.UserId
  local success, data = pcall(function()
    return inventoryStore:GetAsync("inv_" .. userId)
  end)
  inventories[userId] = (success and type(data) == "table" and data) or {}
end

local function saveInventory(player: Player)
  local userId = player.UserId
  if not inventories[userId] then return end
  pcall(function()
    inventoryStore:SetAsync("inv_" .. userId, inventories[userId])
  end)
end

local function getInventory(player: Player): {[number]: InventoryItem?}
  return inventories[player.UserId] or {}
end

local function findItem(player: Player, itemName: string): (number?, InventoryItem?)
  local inv = inventories[player.UserId]
  if not inv then return nil, nil end
  for slot, item in ipairs(inv) do
    if item and item.name == itemName then return slot, item end
  end
  return nil, nil
end

local function findEmptySlot(player: Player): number?
  local inv = inventories[player.UserId]
  if not inv then return nil end
  for i = 1, MAX_SLOTS do
    if not inv[i] then return i end
  end
  return nil
end

local function addItem(player: Player, item: InventoryItem): (boolean, string)
  local userId = player.UserId
  if not inventories[userId] then return false, "Player not found" end

  if STACKABLE then
    local slot, existing = findItem(player, item.name)
    if existing then
      existing.quantity = existing.quantity + item.quantity
      return true, "Stacked"
    end
  end

  local slot = findEmptySlot(player)
  if not slot then return false, "Inventory full" end

  inventories[userId][slot] = item
  return true, "Added"
end

local function removeItem(player: Player, itemName: string, quantity: number): boolean
  local slot, item = findItem(player, itemName)
  if not slot or not item then return false end
  if item.quantity < quantity then return false end
  item.quantity -= quantity
  if item.quantity <= 0 then
    inventories[player.UserId][slot] = nil
  end
  return true
end

-- RemoteFunction API for client inventory UI
local invRemote = Instance.new("RemoteFunction")
invRemote.Name = "InventoryAction"
invRemote.Parent = game:GetService("ReplicatedStorage")

invRemote.OnServerInvoke = function(player: Player, action: string, data: any): any
  if action == "get" then
    return getInventory(player)
  elseif action == "use" and type(data) == "table" then
    return removeItem(player, data.name, data.quantity or 1)
  end
  return false
end

-- Server API via BindableFunction
local invAPI = Instance.new("BindableFunction")
invAPI.Name = "InventoryAPI"
invAPI.Parent = game:GetService("ServerStorage")

invAPI.OnInvoke = function(action: string, player: Player, itemData: InventoryItem?, itemName: string?, qty: number?)
  if action == "add" and itemData then
    return addItem(player, itemData)
  elseif action == "remove" and itemName then
    return removeItem(player, itemName, qty or 1)
  elseif action == "has" and itemName then
    return findItem(player, itemName) ~= nil
  end
end

Players.PlayerAdded:Connect(function(p) loadInventory(p) end)
Players.PlayerRemoving:Connect(function(p) saveInventory(p); inventories[p.UserId] = nil end)

print("[Inventory] System initialized — " .. MAX_SLOTS .. " slots, categories: ${categories.join(', ')}")`
}

