/**
 * script-templates.ts
 *
 * Luau code generation templates per script kind.
 *
 * Each template is a system-prompt prefix that tells the LLM:
 *   - what Roblox APIs it MUST prefer (task.wait, WaitForChild, DataStoreService)
 *   - what patterns are FORBIDDEN (loadstring, require(id), wait(), HttpService in client)
 *   - what the expected file layout looks like (ServerScriptService, ReplicatedStorage)
 *   - security best practices (server-authoritative state, validate remotes)
 *
 * These prefixes are concatenated with the user's script prompt by the chat
 * route / build executor before the final `callAI` call.
 */

export type ScriptKind =
  | 'server'
  | 'localscript'
  | 'module'
  | 'remote-event'
  | 'datastore'
  | 'leaderboard'
  | 'gamepass-handler'
  | 'humanoid-combat'
  | 'tween-animation'
  | 'proximity-prompt'
  | 'generic'

const SECURITY_PREAMBLE = `Security rules (HARD):
- NEVER use loadstring, require(assetId), or HttpService in client code.
- NEVER trust the client: all coin/XP/inventory mutations happen on the SERVER.
- Always validate RemoteEvent/RemoteFunction arguments: typeof, tostring, math.clamp.
- Wrap DataStore calls in pcall and retry up to 3 times with task.wait(2).
- Use task.wait instead of wait(). Use task.spawn instead of spawn().
- Use WaitForChild(name, 10) with a timeout, never the infinite form.
- Prefer Attribute and CollectionService tags over string-keyed state.
- Add a top-line watermark comment: -- Generated by Forje AI`

const STYLE_PREAMBLE = `Style rules:
- Use local for every variable; no globals.
- Use 4-space indentation.
- Use type annotations on function params where obvious: local function foo(n: number): boolean
- Use descriptive names; no single-letter vars except loop indices.
- Guard every service lookup: local Players = game:GetService("Players")
- Wrap the main logic in a function and call it at the end.`

