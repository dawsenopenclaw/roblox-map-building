export function resolveScriptTemplate(kind: string | undefined): string {
  if (!kind) return SCRIPT_TEMPLATES.generic
  const key = kind.toLowerCase().trim() as ScriptKind
  return SCRIPT_TEMPLATES[key] ?? SCRIPT_TEMPLATES.generic
}
