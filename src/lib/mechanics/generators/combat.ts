import type { GameMechanic } from '../../orchestrator/types'
import { safeLuaIdentifier } from '../../luau/identifier'
import { safeLuaString } from '../../luau/string'

export interface Weapon {
  id: string
  name: string
  damage: number
  cooldown: number
  range: number
}

/**
 * Combat mechanic — server-authoritative damage with sanity checks.
 * Generates a CombatHandler server script that registers weapons and validates
 * hit RemoteEvent calls.
 */
export function generateCombatMechanic(weapons: Weapon[] = []): GameMechanic {
  const list: Weapon[] =
    weapons.length > 0
      ? weapons
      : [
          { id: 'sword', name: 'Iron Sword', damage: 18, cooldown: 0.8, range: 8 },
          { id: 'bow', name: 'Hunter Bow', damage: 24, cooldown: 1.2, range: 80 },
        ]

  const weaponsLuau = list
    .map((w) => {
      const key = safeLuaIdentifier(w.id, 'weapon')
      const idLit = safeLuaString(w.id)
      const nameLit = safeLuaString(w.name)
      const dmg = Number.isFinite(w.damage) ? w.damage : 0
      const cd = Number.isFinite(w.cooldown) ? w.cooldown : 1
      const rng = Number.isFinite(w.range) ? w.range : 0
      return `    ${key} = { id = "${idLit}", name = "${nameLit}", damage = ${dmg}, cooldown = ${cd}, range = ${rng} },`
    })
    .join('\n')

  const luauCode = `--!strict
-- CombatHandler — server-authoritative combat with anti-cheat range/cooldown checks
-- Auto-generated by ForjeGames Orchestrator

local Players           = game:GetService("Players")
local ReplicatedStorage = game:GetService("ReplicatedStorage")

type Weapon = { id: string, name: string, damage: number, cooldown: number, range: number }

local WEAPONS: { [string]: Weapon } = {
${weaponsLuau}
}

-- Remotes
local folder = ReplicatedStorage:FindFirstChild("CombatRemotes") or Instance.new("Folder")
folder.Name   = "CombatRemotes"
folder.Parent = ReplicatedStorage

local hit = folder:FindFirstChild("ReportHit") :: RemoteEvent
if not hit then
    hit = Instance.new("RemoteEvent")
    hit.Name   = "ReportHit"
    hit.Parent = folder
end

local getWeapons = folder:FindFirstChild("GetWeapons") :: RemoteFunction
if not getWeapons then
    getWeapons = Instance.new("RemoteFunction")
    getWeapons.Name   = "GetWeapons"
    getWeapons.Parent = folder
end
getWeapons.OnServerInvoke = function(_p) return WEAPONS end

local lastUsed: { [number]: { [string]: number } } = {}

local function getChar(player: Player): (Model?, Humanoid?, BasePart?)
    local char = player.Character
    if not char then return nil, nil, nil end
    local hum  = char:FindFirstChildOfClass("Humanoid")
    local root = char:FindFirstChild("HumanoidRootPart") :: BasePart?
    return char, hum, root
end

hit.OnServerEvent:Connect(function(player: Player, weaponId: any, targetModel: any)
    if typeof(weaponId) ~= "string" then return end
    if typeof(targetModel) ~= "Instance" or not targetModel:IsA("Model") then return end

    local weapon = WEAPONS[weaponId]
    if not weapon then return end

    local userLast = lastUsed[player.UserId] or {}
    lastUsed[player.UserId] = userLast
    local now = os.clock()
    if (now - (userLast[weaponId] or 0)) < weapon.cooldown then
        return
    end
    userLast[weaponId] = now

    local _attacker, _attackerHum, attackerRoot = getChar(player)
    if not attackerRoot then return end

    local targetHum = targetModel:FindFirstChildOfClass("Humanoid")
    local targetRoot = targetModel:FindFirstChild("HumanoidRootPart") :: BasePart?
    if not targetHum or not targetRoot then return end
    if targetHum.Health <= 0 then return end

    -- Range check with a tolerance
    local dist = (attackerRoot.Position - targetRoot.Position).Magnitude
    if dist > (weapon.range + 4) then return end

    targetHum:TakeDamage(weapon.damage)
end)

Players.PlayerRemoving:Connect(function(player)
    lastUsed[player.UserId] = nil
end)

return { Weapons = WEAPONS }
`

  return {
    id: 'combat',
    type: 'combat',
    name: 'Combat Handler',
    scriptPath: 'ServerScriptService/Combat/CombatHandler.server.lua',
    luauCode,
    dependencies: [],
  }
}
