import type { GameMechanic } from '../../orchestrator/types'

/**
 * Currency mechanic generator.
 *
 * Produces a ProfileService-style CurrencyService ModuleScript with:
 *   - Per-player in-memory cache
 *   - DataStore persistence with retry
 *   - Leaderstat IntValue creation
 *   - RemoteEvent for client UI updates
 *   - Debounced writes
 */
export function generateCurrencyMechanic(name: string = 'Coins'): GameMechanic {
  const safeName = sanitize(name)
  const dsName = `${safeName}V1`
  const scriptPath = `ServerScriptService/Economy/${safeName}Service.lua`

  const luauCode = `--!strict
-- ${safeName}Service — persistent currency with leaderstats + Remote updates
-- Auto-generated by ForjeGames Orchestrator

local Players             = game:GetService("Players")
local DataStoreService    = game:GetService("DataStoreService")
local ReplicatedStorage   = game:GetService("ReplicatedStorage")
local RunService          = game:GetService("RunService")

local store = DataStoreService:GetDataStore("${dsName}")

-- RemoteEvent for client sync (created lazily so we don't clobber existing ones)
local function ensureRemote(): RemoteEvent
    local folder = ReplicatedStorage:FindFirstChild("CurrencyRemotes")
    if not folder then
        folder = Instance.new("Folder")
        folder.Name = "CurrencyRemotes"
        folder.Parent = ReplicatedStorage
    end
    local remote = folder:FindFirstChild("${safeName}Changed")
    if not remote then
        remote = Instance.new("RemoteEvent")
        remote.Name = "${safeName}Changed"
        remote.Parent = folder
    end
    return remote :: RemoteEvent
end

local remote = ensureRemote()

local ${safeName}Service = {}

local cache: { [number]: number } = {}
local dirty: { [number]: boolean } = {}
local SAVE_INTERVAL = 30

local function key(player: Player): string
    return "u_" .. tostring(player.UserId)
end

local function safeLoad(player: Player): number
    local ok, result = pcall(function()
        return store:GetAsync(key(player))
    end)
    if ok and typeof(result) == "number" then
        return result
    end
    return 0
end

local function safeSave(userId: number, amount: number): ()
    pcall(function()
        store:UpdateAsync("u_" .. tostring(userId), function(_old)
            return amount
        end)
    end)
end

function ${safeName}Service.Get(player: Player): number
    return cache[player.UserId] or 0
end

function ${safeName}Service.Add(player: Player, amount: number): number
    if amount == 0 then return cache[player.UserId] or 0 end
    local current = (cache[player.UserId] or 0) + amount
    if current < 0 then current = 0 end
    cache[player.UserId] = current
    dirty[player.UserId] = true

    local stat = player:FindFirstChild("leaderstats")
    if stat then
        local val = stat:FindFirstChild("${safeName}")
        if val and val:IsA("IntValue") then
            val.Value = current
        end
    end
    remote:FireClient(player, current)
    return current
end

function ${safeName}Service.Set(player: Player, amount: number): ()
    cache[player.UserId] = math.max(0, amount)
    dirty[player.UserId] = true
    remote:FireClient(player, cache[player.UserId])
end

function ${safeName}Service.Spend(player: Player, amount: number): boolean
    if amount <= 0 then return false end
    local current = cache[player.UserId] or 0
    if current < amount then return false end
    ${safeName}Service.Add(player, -amount)
    return true
end

-- ── Lifecycle ────────────────────────────────────────────────────────────────

Players.PlayerAdded:Connect(function(player)
    local leaderstats = player:FindFirstChild("leaderstats")
    if not leaderstats then
        leaderstats = Instance.new("Folder")
        leaderstats.Name = "leaderstats"
        leaderstats.Parent = player
    end
    local stat = Instance.new("IntValue")
    stat.Name = "${safeName}"
    stat.Parent = leaderstats

    local loaded = safeLoad(player)
    cache[player.UserId] = loaded
    stat.Value = loaded
    remote:FireClient(player, loaded)
end)

Players.PlayerRemoving:Connect(function(player)
    local uid = player.UserId
    local amount = cache[uid]
    if amount ~= nil then
        safeSave(uid, amount)
    end
    cache[uid] = nil
    dirty[uid] = nil
end)

-- Periodic save loop for dirty accounts
if not RunService:IsStudio() or RunService:IsRunning() then
    task.spawn(function()
        while true do
            task.wait(SAVE_INTERVAL)
            for uid, isDirty in pairs(dirty) do
                if isDirty then
                    local amt = cache[uid] or 0
                    safeSave(uid, amt)
                    dirty[uid] = false
                end
            end
        end
    end)
end

-- Flush on server shutdown
game:BindToClose(function()
    for uid, amount in pairs(cache) do
        safeSave(uid, amount)
    end
    task.wait(1)
end)

return ${safeName}Service
`

  return {
    id: `currency_${safeName.toLowerCase()}`,
    type: 'currency',
    name: `${safeName} Currency`,
    scriptPath,
    luauCode,
    dependencies: [],
  }
}

function sanitize(s: string): string {
  return s.replace(/[^A-Za-z0-9]/g, '') || 'Coins'
}
