/**
 * Notification Toast Template — corner toasts (success/info/warn/error).
 */

import type { ThemePreset } from '@/lib/theme/theme-presets'

export function notificationToastTemplate(theme: ThemePreset): string {
  const { primary, secondary, accent, highlight } = theme.colorPalette

  return `-- Notification Toast UI (auto-generated by ForjeGames UI Kit)
-- Theme: ${theme.name}
-- LocalScript in StarterPlayerScripts.
-- Fire ReplicatedStorage.ShowToast with { type = "success", title = "...", message = "..." }

local Players = game:GetService("Players")
local ReplicatedStorage = game:GetService("ReplicatedStorage")
local TweenService = game:GetService("TweenService")

local player = Players.LocalPlayer
local playerGui = player:WaitForChild("PlayerGui")

local COLORS = {
    primary   = Color3.fromHex("${primary}"),
    secondary = Color3.fromHex("${secondary}"),
    accent    = Color3.fromHex("${accent}"),
    highlight = Color3.fromHex("${highlight}"),
    bg        = Color3.fromRGB(22, 22, 30),
    text      = Color3.fromRGB(245, 245, 250),
    success   = Color3.fromRGB(90, 200, 110),
    info      = Color3.fromRGB(80, 160, 255),
    warn      = Color3.fromRGB(255, 180, 60),
    error_    = Color3.fromRGB(240, 80, 80),
}

local ICONS = {
    success = "\u{2705}",
    info    = "\u{2139}",
    warn    = "\u{26A0}",
    error_  = "\u{274C}",
}

local gui = Instance.new("ScreenGui")
gui.Name = "ForjeToasts"
gui.ResetOnSpawn = false
gui.IgnoreGuiInset = true
gui.DisplayOrder = 100
gui.Parent = playerGui

local stack = Instance.new("Frame")
stack.Size = UDim2.new(0, 340, 1, -40)
stack.Position = UDim2.new(1, -360, 0, 20)
stack.BackgroundTransparency = 1
stack.Parent = gui

local stackLayout = Instance.new("UIListLayout")
stackLayout.Padding = UDim.new(0, 10)
stackLayout.VerticalAlignment = Enum.VerticalAlignment.Top
stackLayout.SortOrder = Enum.SortOrder.LayoutOrder
stackLayout.Parent = stack

local toastId = 0

local function pushToast(kind, title, message)
    toastId = toastId + 1
    local mine = toastId

    local sideColor = (kind == "success" and COLORS.success)
                   or (kind == "info" and COLORS.info)
                   or (kind == "warn" and COLORS.warn)
                   or (kind == "error" and COLORS.error_)
                   or COLORS.accent

    local toast = Instance.new("Frame")
    toast.Name = "Toast" .. mine
    toast.Size = UDim2.new(1, 0, 0, 72)
    toast.BackgroundColor3 = COLORS.bg
    toast.BackgroundTransparency = 0.05
    toast.BorderSizePixel = 0
    toast.LayoutOrder = mine
    toast.Parent = stack

    local c = Instance.new("UICorner")
    c.CornerRadius = UDim.new(0, 10)
    c.Parent = toast

    local s = Instance.new("UIStroke")
    s.Thickness = 2
    s.Color = sideColor
    s.Parent = toast

    local sideBar = Instance.new("Frame")
    sideBar.Size = UDim2.new(0, 6, 1, 0)
    sideBar.BackgroundColor3 = sideColor
    sideBar.BorderSizePixel = 0
    sideBar.Parent = toast

    local sideCorner = Instance.new("UICorner")
    sideCorner.CornerRadius = UDim.new(0, 3)
    sideCorner.Parent = sideBar

    local icon = Instance.new("TextLabel")
    icon.Size = UDim2.new(0, 40, 0, 40)
    icon.Position = UDim2.new(0, 14, 0.5, -20)
    icon.BackgroundTransparency = 1
    icon.Font = Enum.Font.GothamBlack
    icon.TextScaled = true
    icon.Text = ICONS[kind == "error" and "error_" or kind] or "\u{2139}"
    icon.TextColor3 = sideColor
    icon.Parent = toast

    local titleLabel = Instance.new("TextLabel")
    titleLabel.Size = UDim2.new(1, -80, 0, 22)
    titleLabel.Position = UDim2.new(0, 60, 0, 10)
    titleLabel.BackgroundTransparency = 1
    titleLabel.Font = Enum.Font.GothamBold
    titleLabel.TextScaled = true
    titleLabel.Text = title or ""
    titleLabel.TextColor3 = COLORS.highlight
    titleLabel.TextXAlignment = Enum.TextXAlignment.Left
    titleLabel.Parent = toast

    local msg = Instance.new("TextLabel")
    msg.Size = UDim2.new(1, -80, 0, 22)
    msg.Position = UDim2.new(0, 60, 0, 36)
    msg.BackgroundTransparency = 1
    msg.Font = Enum.Font.Gotham
    msg.TextScaled = true
    msg.Text = message or ""
    msg.TextColor3 = COLORS.text
    msg.TextXAlignment = Enum.TextXAlignment.Left
    msg.TextTruncate = Enum.TextTruncate.AtEnd
    msg.Parent = toast

    -- Slide in
    toast.Position = UDim2.new(1, 20, 0, 0)
    TweenService:Create(toast, TweenInfo.new(0.35, Enum.EasingStyle.Back), {
        Position = UDim2.new(0, 0, 0, 0),
    }):Play()

    task.delay(4.0, function()
        local fadeOut = TweenService:Create(toast, TweenInfo.new(0.3), {
            BackgroundTransparency = 1,
        })
        fadeOut:Play()
        for _, c in ipairs(toast:GetDescendants()) do
            if c:IsA("TextLabel") then
                TweenService:Create(c, TweenInfo.new(0.3), { TextTransparency = 1 }):Play()
            elseif c:IsA("Frame") then
                TweenService:Create(c, TweenInfo.new(0.3), { BackgroundTransparency = 1 }):Play()
            elseif c:IsA("UIStroke") then
                TweenService:Create(c, TweenInfo.new(0.3), { Transparency = 1 }):Play()
            end
        end
        fadeOut.Completed:Wait()
        toast:Destroy()
    end)
end

local event = ReplicatedStorage:FindFirstChild("ShowToast")
if not event then
    event = Instance.new("RemoteEvent")
    event.Name = "ShowToast"
    event.Parent = ReplicatedStorage
end

event.OnClientEvent:Connect(function(payload)
    payload = payload or {}
    pushToast(payload.type or "info", payload.title or "", payload.message or "")
end)

-- Bindable for local-only toasts
local localBindable = Instance.new("BindableEvent")
localBindable.Name = "ShowLocalToast"
localBindable.Parent = gui
localBindable.Event:Connect(function(payload)
    payload = payload or {}
    pushToast(payload.type or "info", payload.title or "", payload.message or "")
end)

print("[ForjeToasts] ready")
`
}
