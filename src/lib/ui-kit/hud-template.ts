/**
 * HUD Template — in-game heads-up display.
 *
 * Returns a Luau source string that creates a production-quality ScreenGui
 * containing a health bar, coin counter, level badge, and minimap stub.
 * Colors are pulled from the active ThemePreset so the HUD auto-matches the
 * rest of the build.
 */

import type { ThemePreset } from '@/lib/theme/theme-presets'

export function hudTemplate(theme: ThemePreset): string {
  const { primary, secondary, accent, highlight } = theme.colorPalette

  return `-- HUD (auto-generated by ForjeGames UI Kit)
-- Theme: ${theme.name}
-- Drop this LocalScript into StarterPlayerScripts.

local Players = game:GetService("Players")
local TweenService = game:GetService("TweenService")

local player = Players.LocalPlayer
local playerGui = player:WaitForChild("PlayerGui")

-- Root ScreenGui
local gui = Instance.new("ScreenGui")
gui.Name = "ForjeHUD"
gui.ResetOnSpawn = false
gui.ZIndexBehavior = Enum.ZIndexBehavior.Sibling
gui.IgnoreGuiInset = true
gui.Parent = playerGui

-- Color palette derived from the active theme (${theme.id})
local COLORS = {
    primary   = Color3.fromHex("${primary}"),
    secondary = Color3.fromHex("${secondary}"),
    accent    = Color3.fromHex("${accent}"),
    highlight = Color3.fromHex("${highlight}"),
    danger    = Color3.fromRGB(220, 70, 70),
    bg        = Color3.fromRGB(18, 18, 24),
    text      = Color3.fromRGB(245, 245, 250),
}

local function styledFrame(parent, size, position)
    local f = Instance.new("Frame")
    f.Size = size
    f.Position = position
    f.BackgroundColor3 = COLORS.bg
    f.BackgroundTransparency = 0.2
    f.BorderSizePixel = 0
    f.Parent = parent

    local corner = Instance.new("UICorner")
    corner.CornerRadius = UDim.new(0, 10)
    corner.Parent = f

    local stroke = Instance.new("UIStroke")
    stroke.Thickness = 2
    stroke.Color = COLORS.accent
    stroke.Transparency = 0.35
    stroke.ApplyStrokeMode = Enum.ApplyStrokeMode.Border
    stroke.Parent = f

    local pad = Instance.new("UIPadding")
    pad.PaddingTop = UDim.new(0, 8)
    pad.PaddingBottom = UDim.new(0, 8)
    pad.PaddingLeft = UDim.new(0, 12)
    pad.PaddingRight = UDim.new(0, 12)
    pad.Parent = f

    return f
end

-- Health bar (top-left)
local healthFrame = styledFrame(gui, UDim2.new(0, 260, 0, 48), UDim2.new(0, 20, 0, 20))
healthFrame.Name = "HealthBar"

local healthLabel = Instance.new("TextLabel")
healthLabel.Name = "Label"
healthLabel.Size = UDim2.new(1, 0, 0, 16)
healthLabel.BackgroundTransparency = 1
healthLabel.Font = Enum.Font.GothamBold
healthLabel.TextScaled = true
healthLabel.Text = "HEALTH"
healthLabel.TextColor3 = COLORS.text
healthLabel.TextXAlignment = Enum.TextXAlignment.Left
healthLabel.Parent = healthFrame

local healthBg = Instance.new("Frame")
healthBg.Name = "Background"
healthBg.Size = UDim2.new(1, 0, 0, 14)
healthBg.Position = UDim2.new(0, 0, 0, 18)
healthBg.BackgroundColor3 = Color3.fromRGB(45, 45, 55)
healthBg.BorderSizePixel = 0
healthBg.Parent = healthFrame

local healthBgCorner = Instance.new("UICorner")
healthBgCorner.CornerRadius = UDim.new(1, 0)
healthBgCorner.Parent = healthBg

local healthFill = Instance.new("Frame")
healthFill.Name = "Fill"
healthFill.Size = UDim2.new(1, 0, 1, 0)
healthFill.BackgroundColor3 = Color3.fromRGB(90, 220, 120)
healthFill.BorderSizePixel = 0
healthFill.Parent = healthBg

local healthFillCorner = Instance.new("UICorner")
healthFillCorner.CornerRadius = UDim.new(1, 0)
healthFillCorner.Parent = healthFill

local healthGradient = Instance.new("UIGradient")
healthGradient.Color = ColorSequence.new({
    ColorSequenceKeypoint.new(0, Color3.fromRGB(140, 240, 160)),
    ColorSequenceKeypoint.new(1, Color3.fromRGB(60, 180, 90)),
})
healthGradient.Rotation = 90
healthGradient.Parent = healthFill

local healthText = Instance.new("TextLabel")
healthText.Name = "HealthText"
healthText.Size = UDim2.new(1, 0, 1, 0)
healthText.BackgroundTransparency = 1
healthText.Font = Enum.Font.GothamBold
healthText.TextScaled = true
healthText.Text = "100 / 100"
healthText.TextColor3 = COLORS.text
healthText.TextStrokeTransparency = 0.4
healthText.Parent = healthBg

-- Coins / currency (top-right)
local coinFrame = styledFrame(gui, UDim2.new(0, 180, 0, 48), UDim2.new(1, -200, 0, 20))
coinFrame.Name = "CoinCounter"

local coinIcon = Instance.new("TextLabel")
coinIcon.Size = UDim2.new(0, 32, 0, 32)
coinIcon.Position = UDim2.new(0, 0, 0.5, -16)
coinIcon.BackgroundTransparency = 1
coinIcon.Font = Enum.Font.GothamBold
coinIcon.TextScaled = true
coinIcon.Text = "\u{1F4B0}"
coinIcon.TextColor3 = COLORS.highlight
coinIcon.Parent = coinFrame

local coinLabel = Instance.new("TextLabel")
coinLabel.Name = "CoinLabel"
coinLabel.Size = UDim2.new(1, -40, 1, 0)
coinLabel.Position = UDim2.new(0, 40, 0, 0)
coinLabel.BackgroundTransparency = 1
coinLabel.Font = Enum.Font.GothamBlack
coinLabel.TextScaled = true
coinLabel.Text = "0"
coinLabel.TextColor3 = COLORS.highlight
coinLabel.TextXAlignment = Enum.TextXAlignment.Left
coinLabel.TextStrokeTransparency = 0.6
coinLabel.Parent = coinFrame

-- Level badge
local levelFrame = styledFrame(gui, UDim2.new(0, 120, 0, 40), UDim2.new(1, -140, 0, 78))
levelFrame.Name = "LevelBadge"

local levelLabel = Instance.new("TextLabel")
levelLabel.Size = UDim2.new(1, 0, 1, 0)
levelLabel.BackgroundTransparency = 1
levelLabel.Font = Enum.Font.GothamBold
levelLabel.TextScaled = true
levelLabel.Text = "LVL 1"
levelLabel.TextColor3 = COLORS.accent
levelLabel.Parent = levelFrame

-- Minimap stub (bottom-right)
local mapFrame = styledFrame(gui, UDim2.new(0, 180, 0, 180), UDim2.new(1, -200, 1, -200))
mapFrame.Name = "Minimap"

local mapGradient = Instance.new("UIGradient")
mapGradient.Color = ColorSequence.new({
    ColorSequenceKeypoint.new(0, COLORS.secondary),
    ColorSequenceKeypoint.new(1, COLORS.primary),
})
mapGradient.Rotation = 45
mapGradient.Parent = mapFrame

local mapPlayer = Instance.new("Frame")
mapPlayer.Name = "PlayerDot"
mapPlayer.Size = UDim2.new(0, 10, 0, 10)
mapPlayer.AnchorPoint = Vector2.new(0.5, 0.5)
mapPlayer.Position = UDim2.new(0.5, 0, 0.5, 0)
mapPlayer.BackgroundColor3 = COLORS.highlight
mapPlayer.BorderSizePixel = 0
mapPlayer.ZIndex = 2
mapPlayer.Parent = mapFrame

local mapDotCorner = Instance.new("UICorner")
mapDotCorner.CornerRadius = UDim.new(1, 0)
mapDotCorner.Parent = mapPlayer

-- Reactivity: health binds to Humanoid
local function bindCharacter(character)
    local hum = character:WaitForChild("Humanoid")
    local function update()
        local pct = math.clamp(hum.Health / math.max(hum.MaxHealth, 1), 0, 1)
        TweenService:Create(healthFill, TweenInfo.new(0.25), {
            Size = UDim2.new(pct, 0, 1, 0),
        }):Play()
        healthText.Text = string.format("%d / %d", math.ceil(hum.Health), hum.MaxHealth)
        if pct < 0.3 then
            healthFill.BackgroundColor3 = COLORS.danger
        end
    end
    hum.HealthChanged:Connect(update)
    update()
end

if player.Character then bindCharacter(player.Character) end
player.CharacterAdded:Connect(bindCharacter)

-- Public BindableEvents the server can fire to update counters
local setCoin = Instance.new("BindableEvent")
setCoin.Name = "SetCoin"
setCoin.Parent = gui
setCoin.Event:Connect(function(n) coinLabel.Text = tostring(n) end)

local setLevel = Instance.new("BindableEvent")
setLevel.Name = "SetLevel"
setLevel.Parent = gui
setLevel.Event:Connect(function(n) levelLabel.Text = "LVL " .. tostring(n) end)

print("[ForjeHUD] initialized with ${theme.name} theme")
`
}
