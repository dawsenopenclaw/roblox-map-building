/**
 * Inventory Template — grid-based inventory with item slots and drag affordance.
 */

import type { ThemePreset } from '@/lib/theme/theme-presets'

export function inventoryTemplate(theme: ThemePreset): string {
  const { primary, secondary, accent, highlight } = theme.colorPalette

  return `-- Inventory UI (auto-generated by ForjeGames UI Kit)
-- Theme: ${theme.name}
-- Place as LocalScript under StarterPlayerScripts.

local Players = game:GetService("Players")
local TweenService = game:GetService("TweenService")
local UserInputService = game:GetService("UserInputService")

local player = Players.LocalPlayer
local playerGui = player:WaitForChild("PlayerGui")

local COLORS = {
    primary   = Color3.fromHex("${primary}"),
    secondary = Color3.fromHex("${secondary}"),
    accent    = Color3.fromHex("${accent}"),
    highlight = Color3.fromHex("${highlight}"),
    bg        = Color3.fromRGB(20, 20, 26),
    slot      = Color3.fromRGB(36, 36, 44),
    slotHover = Color3.fromRGB(52, 52, 62),
    text      = Color3.fromRGB(240, 240, 245),
    muted     = Color3.fromRGB(140, 140, 155),
}

local SLOT_COUNT = 30 -- 6x5 grid

-- Demo items — in production this would sync from a server RemoteEvent.
local DEMO_ITEMS = {
    [1]  = { icon = "\u{2694}",  name = "Iron Sword",   count = 1 },
    [2]  = { icon = "\u{1F6E1}", name = "Wood Shield",  count = 1 },
    [3]  = { icon = "\u{1F9EA}", name = "Potion",       count = 12 },
    [4]  = { icon = "\u{1F9F1}", name = "Brick",        count = 64 },
    [5]  = { icon = "\u{1F4A0}", name = "Gem",          count = 4 },
    [9]  = { icon = "\u{1F356}", name = "Roast",        count = 3 },
    [11] = { icon = "\u{1F4DC}", name = "Scroll",       count = 2 },
}

local gui = Instance.new("ScreenGui")
gui.Name = "ForjeInventory"
gui.ResetOnSpawn = false
gui.IgnoreGuiInset = true
gui.Enabled = false
gui.Parent = playerGui

local dim = Instance.new("Frame")
dim.Size = UDim2.fromScale(1, 1)
dim.BackgroundColor3 = Color3.new(0, 0, 0)
dim.BackgroundTransparency = 0.55
dim.BorderSizePixel = 0
dim.Parent = gui

local window = Instance.new("Frame")
window.Name = "Window"
window.Size = UDim2.new(0, 640, 0, 520)
window.AnchorPoint = Vector2.new(0.5, 0.5)
window.Position = UDim2.fromScale(0.5, 0.5)
window.BackgroundColor3 = COLORS.bg
window.BorderSizePixel = 0
window.Parent = gui

local corner = Instance.new("UICorner")
corner.CornerRadius = UDim.new(0, 16)
corner.Parent = window

local stroke = Instance.new("UIStroke")
stroke.Thickness = 3
stroke.Color = COLORS.accent
stroke.Transparency = 0.25
stroke.Parent = window

local grad = Instance.new("UIGradient")
grad.Color = ColorSequence.new({
    ColorSequenceKeypoint.new(0, COLORS.bg),
    ColorSequenceKeypoint.new(1, COLORS.secondary),
})
grad.Rotation = 120
grad.Parent = window

-- Header
local header = Instance.new("Frame")
header.Size = UDim2.new(1, 0, 0, 56)
header.BackgroundColor3 = COLORS.primary
header.BorderSizePixel = 0
header.Parent = window

local headerCorner = Instance.new("UICorner")
headerCorner.CornerRadius = UDim.new(0, 16)
headerCorner.Parent = header

local headerMask = Instance.new("Frame")
headerMask.Size = UDim2.new(1, 0, 0, 16)
headerMask.Position = UDim2.new(0, 0, 1, -16)
headerMask.BackgroundColor3 = COLORS.primary
headerMask.BorderSizePixel = 0
headerMask.Parent = header

local title = Instance.new("TextLabel")
title.Size = UDim2.new(1, -80, 1, 0)
title.Position = UDim2.new(0, 20, 0, 0)
title.BackgroundTransparency = 1
title.Font = Enum.Font.GothamBlack
title.TextScaled = true
title.Text = "\u{1F392}  INVENTORY"
title.TextColor3 = COLORS.highlight
title.TextXAlignment = Enum.TextXAlignment.Left
title.Parent = header

local closeBtn = Instance.new("TextButton")
closeBtn.Size = UDim2.new(0, 36, 0, 36)
closeBtn.Position = UDim2.new(1, -48, 0.5, -18)
closeBtn.BackgroundColor3 = Color3.fromRGB(200, 60, 60)
closeBtn.BorderSizePixel = 0
closeBtn.Font = Enum.Font.GothamBlack
closeBtn.Text = "X"
closeBtn.TextColor3 = Color3.new(1, 1, 1)
closeBtn.TextScaled = true
closeBtn.Parent = header

local closeCorner = Instance.new("UICorner")
closeCorner.CornerRadius = UDim.new(1, 0)
closeCorner.Parent = closeBtn

-- Slot grid
local grid = Instance.new("Frame")
grid.Size = UDim2.new(1, -32, 1, -80)
grid.Position = UDim2.new(0, 16, 0, 68)
grid.BackgroundTransparency = 1
grid.Parent = window

local layout = Instance.new("UIGridLayout")
layout.CellSize = UDim2.new(0, 96, 0, 96)
layout.CellPadding = UDim2.new(0, 6, 0, 6)
layout.SortOrder = Enum.SortOrder.LayoutOrder
layout.Parent = grid

local tooltip = Instance.new("TextLabel")
tooltip.Size = UDim2.new(0, 160, 0, 32)
tooltip.BackgroundColor3 = Color3.fromRGB(10, 10, 14)
tooltip.BackgroundTransparency = 0.1
tooltip.BorderSizePixel = 0
tooltip.Font = Enum.Font.GothamBold
tooltip.TextScaled = true
tooltip.TextColor3 = COLORS.highlight
tooltip.Visible = false
tooltip.ZIndex = 20
tooltip.Parent = gui

local tipCorner = Instance.new("UICorner")
tipCorner.CornerRadius = UDim.new(0, 6)
tipCorner.Parent = tooltip

local function makeSlot(index)
    local slot = Instance.new("TextButton")
    slot.Name = "Slot" .. index
    slot.LayoutOrder = index
    slot.BackgroundColor3 = COLORS.slot
    slot.BorderSizePixel = 0
    slot.AutoButtonColor = false
    slot.Text = ""
    slot.Parent = grid

    local c = Instance.new("UICorner")
    c.CornerRadius = UDim.new(0, 8)
    c.Parent = slot

    local s = Instance.new("UIStroke")
    s.Thickness = 1
    s.Color = COLORS.muted
    s.Transparency = 0.5
    s.Parent = slot

    local icon = Instance.new("TextLabel")
    icon.Name = "Icon"
    icon.Size = UDim2.fromScale(1, 1)
    icon.BackgroundTransparency = 1
    icon.Font = Enum.Font.GothamBlack
    icon.TextScaled = true
    icon.Text = ""
    icon.TextColor3 = COLORS.text
    icon.Parent = slot

    local count = Instance.new("TextLabel")
    count.Name = "Count"
    count.Size = UDim2.new(0, 32, 0, 20)
    count.Position = UDim2.new(1, -34, 1, -22)
    count.BackgroundTransparency = 1
    count.Font = Enum.Font.GothamBold
    count.TextScaled = true
    count.Text = ""
    count.TextColor3 = COLORS.highlight
    count.TextStrokeTransparency = 0.3
    count.Parent = slot

    local item = DEMO_ITEMS[index]
    if item then
        icon.Text = item.icon
        count.Text = item.count > 1 and tostring(item.count) or ""
    end

    slot.MouseEnter:Connect(function()
        TweenService:Create(slot, TweenInfo.new(0.1), {
            BackgroundColor3 = COLORS.slotHover,
        }):Play()
        if item then
            tooltip.Text = item.name
            tooltip.Visible = true
        end
    end)

    slot.MouseLeave:Connect(function()
        TweenService:Create(slot, TweenInfo.new(0.1), {
            BackgroundColor3 = COLORS.slot,
        }):Play()
        tooltip.Visible = false
    end)

    -- Drag affordance: scale on hold
    slot.MouseButton1Down:Connect(function()
        TweenService:Create(slot, TweenInfo.new(0.08), {
            Size = UDim2.new(0, 92, 0, 92),
        }):Play()
    end)
    slot.MouseButton1Up:Connect(function()
        slot.Size = UDim2.new(0, 96, 0, 96)
    end)

    return slot
end

for i = 1, SLOT_COUNT do makeSlot(i) end

UserInputService.InputChanged:Connect(function(input)
    if input.UserInputType == Enum.UserInputType.MouseMovement and tooltip.Visible then
        tooltip.Position = UDim2.new(0, input.Position.X + 16, 0, input.Position.Y + 16)
    end
end)

local function setOpen(state)
    gui.Enabled = state
    window.Size = state and UDim2.new(0, 580, 0, 480) or UDim2.new(0, 640, 0, 520)
    TweenService:Create(window, TweenInfo.new(0.25, Enum.EasingStyle.Back), {
        Size = UDim2.new(0, 640, 0, 520),
    }):Play()
end

closeBtn.MouseButton1Click:Connect(function() setOpen(false) end)

UserInputService.InputBegan:Connect(function(input, gp)
    if gp then return end
    if input.KeyCode == Enum.KeyCode.E or input.KeyCode == Enum.KeyCode.Tab then
        setOpen(not gui.Enabled)
    end
end)

-- Server-facing update event
local updateSlot = Instance.new("BindableEvent")
updateSlot.Name = "UpdateSlot"
updateSlot.Parent = gui
updateSlot.Event:Connect(function(index, icon, count, name)
    local slot = grid:FindFirstChild("Slot" .. index)
    if not slot then return end
    slot:FindFirstChild("Icon").Text = icon or ""
    slot:FindFirstChild("Count").Text = (count and count > 1) and tostring(count) or ""
end)

print("[ForjeInventory] ready with " .. SLOT_COUNT .. " slots")
`
}
