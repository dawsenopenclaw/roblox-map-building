export type RoofStyle = 'flat' | 'gabled' | 'hip'

export interface BuildRoofParams {
  position: Vector3Param
  sizeX: number
  sizeZ: number
  style: RoofStyle
  material: string
  color?: [number, number, number]
  peakHeight?: number
}

export function buildRoof(params: BuildRoofParams): string {
  const { position, sizeX, sizeZ, style, material, color = [120, 80, 60], peakHeight = 4 } = params

  const colorLit = colorLiteral(color)

  let body = `local roofModel = Instance.new("Model")
roofModel.Name = "Roof_${style}"
roofModel.Parent = workspace\n`

  if (style === 'flat') {
    body += emitPart({
      varName: 'roof',
      name: 'RoofFlat',
      position,
      size: { x: sizeX + 2, y: 1, z: sizeZ + 2 },
      color,
      material,
      parent: 'roofModel',
    })
  } else if (style === 'gabled') {
    // Two wedge slopes meeting at a ridge
    body += `
local halfZ = ${sizeZ / 2}
local slope1 = Instance.new("WedgePart")
slope1.Name = "GableSlope1"
slope1.Size = Vector3.new(${sizeX}, ${peakHeight}, halfZ)
slope1.CFrame = CFrame.new(${position.x}, ${position.y + peakHeight / 2}, ${position.z - sizeZ / 4}) * CFrame.Angles(0, 0, 0)
slope1.Color = ${colorLit}
slope1.Material = Enum.Material.${material}
slope1.Anchored = true
slope1.Parent = roofModel

local slope2 = Instance.new("WedgePart")
slope2.Name = "GableSlope2"
slope2.Size = Vector3.new(${sizeX}, ${peakHeight}, halfZ)
slope2.CFrame = CFrame.new(${position.x}, ${position.y + peakHeight / 2}, ${position.z + sizeZ / 4}) * CFrame.Angles(0, math.pi, 0)
slope2.Color = ${colorLit}
slope2.Material = Enum.Material.${material}
slope2.Anchored = true
slope2.Parent = roofModel`
  } else {
    // Hip roof — four wedge faces
    body += `
local hw = ${sizeX / 2}
local hz = ${sizeZ / 2}

local hipFront = Instance.new("WedgePart")
hipFront.Name = "HipFront"
hipFront.Size = Vector3.new(${sizeX}, ${peakHeight}, ${sizeZ / 2})
hipFront.CFrame = CFrame.new(${position.x}, ${position.y + peakHeight / 2}, ${position.z - sizeZ / 4})
hipFront.Color = ${colorLit}
hipFront.Material = Enum.Material.${material}
hipFront.Anchored = true
hipFront.Parent = roofModel

local hipBack = Instance.new("WedgePart")
hipBack.Name = "HipBack"
hipBack.Size = Vector3.new(${sizeX}, ${peakHeight}, ${sizeZ / 2})
hipBack.CFrame = CFrame.new(${position.x}, ${position.y + peakHeight / 2}, ${position.z + sizeZ / 4}) * CFrame.Angles(0, math.pi, 0)
hipBack.Color = ${colorLit}
hipBack.Material = Enum.Material.${material}
hipBack.Anchored = true
hipBack.Parent = roofModel`
  }

  return wrapWaypoint('BuildRoof', body)
}

export interface BuildStairsParams {
  start: Vector3Param
  end: Vector3Param
  steps: number
  width: number
  material: string
  color?: [number, number, number]
}

export function buildStairs(params: BuildStairsParams): string {
  const { start, end, steps, width, material, color = [180, 160, 140] } = params

  const body = `local stairsModel = Instance.new("Model")
stairsModel.Name = "Stairs"
stairsModel.Parent = workspace

local totalHeight = ${end.y} - ${start.y}
local totalDepth = math.sqrt((${end.x} - ${start.x})^2 + (${end.z} - ${start.z})^2)
local stepH = totalHeight / ${steps}
local stepD = totalDepth / ${steps}
local dirX = (${end.x} - ${start.x}) / ${steps}
local dirZ = (${end.z} - ${start.z}) / ${steps}

for i = 0, ${steps - 1} do
  local step = Instance.new("Part")
  step.Name = "Step_" .. i
  step.Size = Vector3.new(${width}, stepH, stepD)
  step.CFrame = CFrame.new(
    ${start.x} + dirX * i + dirX / 2,
    ${start.y} + stepH * i + stepH / 2,
    ${start.z} + dirZ * i + dirZ / 2
  )
  step.Color = ${colorLiteral(color)}
  step.Material = Enum.Material.${material}
  step.Anchored = true
  step.Parent = stairsModel
end`

  return wrapWaypoint('BuildStairs', body)
}

export type DoorStyle = 'single' | 'double' | 'arch'

export interface BuildDoorParams {
  position: Vector3Param
  size: Vector3Param
  style: DoorStyle
  color?: [number, number, number]
  material?: string
  wallThickness?: number
}

export function buildDoor(params: BuildDoorParams): string {
  const { position, size, style, color = [100, 60, 30], material = 'Wood', wallThickness = 1 } = params
  const frameThick = 0.4

  const body = `local doorModel = Instance.new("Model")
doorModel.Name = "Door_${style}"
doorModel.Parent = workspace

-- Door frame
local frameThick = ${frameThick}
local frameParts = {
  {size = Vector3.new(frameThick, ${size.y}, ${wallThickness + 0.2}), offset = Vector3.new(-${size.x / 2 + frameThick / 2}, 0, 0)},
  {size = Vector3.new(frameThick, ${size.y}, ${wallThickness + 0.2}), offset = Vector3.new(${size.x / 2 + frameThick / 2}, 0, 0)},
  {size = Vector3.new(${size.x + frameThick * 2}, frameThick, ${wallThickness + 0.2}), offset = Vector3.new(0, ${size.y / 2 + frameThick / 2}, 0)},
}
for i, fp in ipairs(frameParts) do
  local f = Instance.new("Part")
  f.Name = "Frame_" .. i
  f.Size = fp.size
  f.CFrame = CFrame.new(${position.x}, ${position.y}, ${position.z}) * CFrame.new(fp.offset)
  f.Color = ${colorLiteral(color)}
  f.Material = Enum.Material.${material}
  f.Anchored = true
  f.Parent = doorModel
end

-- Door panel(s)
${
  style === 'double'
    ? `for _, side in ipairs({-1, 1}) do
  local panel = Instance.new("Part")
  panel.Name = "DoorPanel_" .. side
  panel.Size = Vector3.new(${size.x / 2 - 0.1}, ${size.y - 0.2}, 0.15)
  panel.CFrame = CFrame.new(${position.x} + side * ${size.x / 4}, ${position.y}, ${position.z})
  panel.Color = ${colorLiteral(color)}
  panel.Material = Enum.Material.${material}
  panel.Anchored = true
  panel.Parent = doorModel
end`
    : `local panel = Instance.new("Part")
panel.Name = "DoorPanel"
panel.Size = Vector3.new(${size.x - 0.2}, ${size.y - 0.2}, 0.15)
panel.CFrame = CFrame.new(${position.x}, ${position.y}, ${position.z})
panel.Color = ${colorLiteral(color)}
panel.Material = Enum.Material.${material}
panel.Anchored = true
panel.Parent = doorModel`
}`

  return wrapWaypoint('BuildDoor', body)
}

