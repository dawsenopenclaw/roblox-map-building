// ─── Complex Structures ───────────────────────────────────────────────────────

export interface BuildWallParams {
  start: Vector3Param
  end: Vector3Param
  height: number
  thickness: number
  material: string
  color: [number, number, number]
  windows?: Array<{ offsetAlongWall: number; width: number; windowHeight: number; fromBottom: number }>
}

export function buildWall(params: BuildWallParams): string {
  const { start, end, height, thickness, material, color, windows = [] } = params

  const body = `local model = Instance.new("Model")
model.Name = "Wall"
model.Parent = workspace

local dx = ${end.x} - ${start.x}
local dz = ${end.z} - ${start.z}
local length = math.sqrt(dx * dx + dz * dz)
local angle = math.atan2(dx, dz)
local midX = (${start.x} + ${end.x}) / 2
local midZ = (${start.z} + ${end.z}) / 2
local midY = ${start.y} + ${height / 2}

local wall = Instance.new("Part")
wall.Name = "WallBody"
wall.Size = Vector3.new(${thickness}, ${height}, length)
wall.CFrame = CFrame.new(midX, midY, midZ) * CFrame.Angles(0, angle, 0)
wall.Color = ${colorLiteral(color)}
wall.Material = Enum.Material.${material}
wall.Anchored = true
wall.Parent = model
${
  windows.length === 0
    ? ''
    : `
-- Window cutouts (visual recesses)
${windows
  .map((w, i) => {
    const wCenterY = w.fromBottom + w.windowHeight / 2
    return `local win${i} = Instance.new("Part")
win${i}.Name = "WindowRecess_${i}"
win${i}.Size = Vector3.new(${thickness + 0.2}, ${w.windowHeight}, ${w.width})
win${i}.CFrame = CFrame.new(midX, ${start.y} + ${wCenterY}, midZ) * CFrame.Angles(0, angle, 0) * CFrame.new(0, 0, ${w.offsetAlongWall - length / 2})
win${i}.Color = Color3.fromRGB(140, 200, 220)
win${i}.Material = Enum.Material.Glass
win${i}.Transparency = 0.5
win${i}.Anchored = true
win${i}.Parent = model`
  })
  .join('\n')}
`
}`

  return wrapWaypoint('BuildWall', body)
}

