/**
 * Roblox Builders — pure code-generation functions for every Roblox Studio
 * building operation.  Every exported function returns a self-contained Luau
 * string that can be pasted into the Command Bar or run via robloxstudio_run_script.
 *
 * Design rules:
 *  - Each function is stateless and pure (same params → same output)
 *  - All generated scripts wrap mutations in ChangeHistoryService waypoints
 *  - Realistic stud dimensions (characters are ~5 studs tall)
 *  - Materials and colors map to Roblox Enum.Material / Color3.fromRGB values
 *  - Complex structures group parts into a Model with a descriptive Name
 *  - All anchored = true by default; physics parts are explicit
 *  - No global variable leakage across generated scripts
 */

// ─── Shared helpers ───────────────────────────────────────────────────────────

/** Normalise a Color3-ish value to a Luau Color3.fromRGB(...) literal */
function colorLiteral(color: [number, number, number] | string): string {
  if (Array.isArray(color)) {
    return `Color3.fromRGB(${color[0]}, ${color[1]}, ${color[2]})`
  }
  return color // caller supplied a raw literal like 'Color3.new(1,1,1)'
}

/** Normalise a Vector3-ish value to a Luau Vector3.new(...) literal */
function v3(vec: Vector3Param): string {
  return `Vector3.new(${vec.x}, ${vec.y}, ${vec.z})`
}

function cf(vec: Vector3Param): string {
  return `CFrame.new(${vec.x}, ${vec.y}, ${vec.z})`
}

/** Wrap a Luau script body in a ChangeHistoryService waypoint pair */
function wrapWaypoint(name: string, body: string): string {
  return `-- Auto-generated by ForjeGames roblox-builders
local ChangeHistoryService = game:GetService("ChangeHistoryService")
ChangeHistoryService:SetWaypoint("Before_${name}")

${body}

ChangeHistoryService:SetWaypoint("After_${name}")
`
}

/** Emit a Luau block that creates a BasePart with common properties set */
function emitPart(opts: {
  varName: string
  partClass?: string
  parent?: string
  name: string
  position: Vector3Param
  size: Vector3Param
  color: [number, number, number] | string
  material: string
  anchored?: boolean
  castShadow?: boolean
  transparency?: number
  cframe?: string
}): string {
  const {
    varName,
    partClass = 'Part',
    parent = 'workspace',
    name,
    position,
    size,
    color,
    material,
    anchored = true,
    castShadow = true,
    transparency = 0,
    cframe,
  } = opts

  const pos = cframe ?? cf(position)

  return `local ${varName} = Instance.new("${partClass}")
${varName}.Name = "${name}"
${varName}.Size = ${v3(size)}
${varName}.CFrame = ${pos}
${varName}.Color = ${colorLiteral(color)}
${varName}.Material = Enum.Material.${material}
${varName}.Anchored = ${anchored}
${varName}.CastShadow = ${castShadow}
${varName}.Transparency = ${transparency}
${varName}.Parent = ${parent}`
}

