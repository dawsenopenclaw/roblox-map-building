export type FenceStyle = 'wood' | 'iron' | 'chain'

export interface BuildFenceParams {
  points: Vector3Param[]
  height: number
  style: FenceStyle
  spacing?: number
}

export function buildFence(params: BuildFenceParams): string {
  const { points, height, style, spacing = 3 } = params

  const colors: Record<FenceStyle, string> = {
    wood: 'Color3.fromRGB(120, 80, 50)',
    iron: 'Color3.fromRGB(60, 60, 70)',
    chain: 'Color3.fromRGB(140, 130, 100)',
  }
  const materials: Record<FenceStyle, string> = {
    wood: 'Wood',
    iron: 'Metal',
    chain: 'Metal',
  }

  const body = `local fenceModel = Instance.new("Model")
fenceModel.Name = "Fence_${style}"
fenceModel.Parent = workspace

local fenceColor = ${colors[style]}
local fenceMat = Enum.Material.${materials[style]}
local postSize = Vector3.new(0.4, ${height}, 0.4)
local railH = ${height * 0.7}
local railSize = Vector3.new(0.2, 0.2, 0)

local pts = {
${points.map((p) => `  Vector3.new(${p.x}, ${p.y}, ${p.z}),`).join('\n')}
}

for i = 1, #pts - 1 do
  local a, b = pts[i], pts[i + 1]
  local dx, dz = b.X - a.X, b.Z - a.Z
  local segLen = math.sqrt(dx*dx + dz*dz)
  local angle = math.atan2(dx, dz)
  local numPosts = math.ceil(segLen / ${spacing}) + 1

  -- Rail beam
  local rail = Instance.new("Part")
  rail.Name = "Rail_" .. i
  rail.Size = Vector3.new(0.2, 0.2, segLen)
  rail.CFrame = CFrame.new((a.X+b.X)/2, a.Y + railH, (a.Z+b.Z)/2) * CFrame.Angles(0, angle, 0)
  rail.Color = fenceColor
  rail.Material = fenceMat
  rail.Anchored = true
  rail.Parent = fenceModel

  -- Posts
  for j = 0, numPosts - 1 do
    local t = j / math.max(numPosts - 1, 1)
    local px = a.X + dx * t
    local pz = a.Z + dz * t
    local post = Instance.new("Part")
    post.Name = "Post_" .. i .. "_" .. j
    post.Size = postSize
    post.CFrame = CFrame.new(px, a.Y + ${height / 2}, pz)
    post.Color = fenceColor
    post.Material = fenceMat
    post.Anchored = true
    post.Parent = fenceModel
  end
end`

  return wrapWaypoint('BuildFence', body)
}

export type BridgeStyle = 'wood' | 'stone' | 'suspension'

export interface BuildBridgeParams {
  start: Vector3Param
  end: Vector3Param
  width: number
  style: BridgeStyle
}

export function buildBridge(params: BuildBridgeParams): string {
  const { start, end, width, style } = params

  const body = `local bridgeModel = Instance.new("Model")
bridgeModel.Name = "Bridge_${style}"
bridgeModel.Parent = workspace

local ax, ay, az = ${start.x}, ${start.y}, ${start.z}
local bx, by, bz = ${end.x}, ${end.y}, ${end.z}
local dx, dz = bx - ax, bz - az
local length = math.sqrt(dx*dx + dz*dz)
local angle = math.atan2(dx, dz)
local midX, midY, midZ = (ax+bx)/2, (ay+by)/2, (az+bz)/2

-- Deck
local deck = Instance.new("Part")
deck.Name = "BridgeDeck"
deck.Size = Vector3.new(${width}, 0.8, length)
deck.CFrame = CFrame.new(midX, midY, midZ) * CFrame.Angles(0, angle, 0)
deck.Color = ${style === 'wood' ? 'Color3.fromRGB(120, 80, 50)' : style === 'stone' ? 'Color3.fromRGB(150, 140, 130)' : 'Color3.fromRGB(80, 80, 90)'}
deck.Material = ${style === 'wood' ? 'Enum.Material.Wood' : style === 'stone' ? 'Enum.Material.Cobblestone' : 'Enum.Material.Metal'}
deck.Anchored = true
deck.Parent = bridgeModel

${
  style === 'suspension'
    ? `-- Suspension towers
for _, side in ipairs({-1, 1}) do
  local tower = Instance.new("Part")
  tower.Name = "Tower_" .. side
  tower.Size = Vector3.new(1, 12, 1)
  tower.CFrame = CFrame.new(midX + side * (length * 0.35), midY + 6, midZ) * CFrame.Angles(0, angle, 0)
  tower.Color = Color3.fromRGB(80, 80, 90)
  tower.Material = Enum.Material.Metal
  tower.Anchored = true
  tower.Parent = bridgeModel
end`
    : `-- Side rails
for _, side in ipairs({-1, 1}) do
  local rail = Instance.new("Part")
  rail.Name = "Rail_" .. side
  rail.Size = Vector3.new(0.4, 3, length)
  rail.CFrame = CFrame.new(midX, midY + 1.5, midZ) * CFrame.Angles(0, angle, 0) * CFrame.new(side * ${width / 2}, 0, 0)
  rail.Color = deck.Color
  rail.Material = deck.Material
  rail.Anchored = true
  rail.Parent = bridgeModel
end`
}`

  return wrapWaypoint('BuildBridge', body)
}

