export type Biome = 'grass' | 'desert' | 'snow' | 'forest' | 'swamp' | 'volcanic'

export interface BuildTerrainParams {
  biome: Biome
  sizeX: number
  sizeZ: number
  heightScale?: number
  waterLevel?: number
  position?: Vector3Param
}

export function buildTerrain(params: BuildTerrainParams): string {
  const { biome, sizeX, sizeZ, heightScale = 20, waterLevel = 0, position = { x: 0, y: 0, z: 0 } } = params

  const biomeMaterials: Record<Biome, { surface: string; fill: string; water: string }> = {
    grass: { surface: 'Grass', fill: 'Ground', water: 'Water' },
    desert: { surface: 'Sand', fill: 'Sand', water: 'Water' },
    snow: { surface: 'Snow', fill: 'Glacier', water: 'Ice' },
    forest: { surface: 'Grass', fill: 'Mud', water: 'Water' },
    swamp: { surface: 'Mud', fill: 'Ground', water: 'Water' },
    volcanic: { surface: 'Basalt', fill: 'Basalt', water: 'Water' },
  }

  const mats = biomeMaterials[biome]

  const body = `local terrain = workspace.Terrain
terrain:Clear()

local sizeX = ${sizeX}
local sizeZ = ${sizeZ}
local cellSize = 4  -- Roblox terrain cell size in studs
local heightScale = ${heightScale}
local waterLevel = ${waterLevel}
local ox, oy, oz = ${position.x}, ${position.y}, ${position.z}

-- Simple noise-based heightmap
for xi = 0, math.floor(sizeX / cellSize) do
  for zi = 0, math.floor(sizeZ / cellSize) do
    local wx = ox + xi * cellSize - sizeX / 2
    local wz = oz + zi * cellSize - sizeZ / 2

    -- Layered noise for natural terrain
    local n1 = math.noise(wx * 0.008, wz * 0.008) * 1.0
    local n2 = math.noise(wx * 0.02, wz * 0.02) * 0.4
    local n3 = math.noise(wx * 0.06, wz * 0.06) * 0.1
    local height = (n1 + n2 + n3) * heightScale + oy

    if height < waterLevel then
      -- Fill below water with water material
      terrain:FillBlock(
        CFrame.new(wx, (height + waterLevel) / 2, wz),
        Vector3.new(cellSize, waterLevel - height, cellSize),
        Enum.Material.${mats.water}
      )
    else
      local fillHeight = height - oy
      if fillHeight > 1 then
        terrain:FillBlock(
          CFrame.new(wx, oy + fillHeight / 2, wz),
          Vector3.new(cellSize, fillHeight, cellSize),
          Enum.Material.${mats.fill}
        )
      end
      -- Surface cap
      terrain:FillBlock(
        CFrame.new(wx, height + 0.5, wz),
        Vector3.new(cellSize, 1, cellSize),
        Enum.Material.${mats.surface}
      )
    end
  end
end

print("Terrain generated: ${biome} biome, " .. sizeX .. "x" .. sizeZ .. " studs")`

  return wrapWaypoint('BuildTerrain', body)
}

